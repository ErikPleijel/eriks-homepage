        <h2 class="chapter-heading">In Service of Something Higher</h2>

        <p class="chapter-text">
            Few have embodied this calling as fully as Dag Hammarskjöld. The Swedish
            diplomat served as UN Secretary-General from 1953 until his death in a
            plane crash over the Congo in 1961 – in service to the very end.
        </p>

        <p class="chapter-text">
            He understood that once he took the oath of office, he was no longer
            loyal to a nation, but to the Truth. He was not to serve Sweden, or the
            West, or any "coalition of the righteous," but the UN Charter. Once he
            put on the robe of the referee, he was not allowed to take sides.
        </p>

        <p class="chapter-text">
            The demands of that role were high: impartiality towards special
            interests, courage to withstand pressure from the superpowers, respect
            for law and truth, just-mindedness that answered to principle rather than
            power, integrity even when it cost him. It was a selfless service that
            erases the ego. These are the classical virtues dressed in institutional
            clothing.
        </p>

        <p class="chapter-text">
            He was no starry-eyed idealist. He was a hard realist who saw the
            unsettling truth: these institutions are the only bulwark against the
            abyss of might-makes-right. "The UN was not created to take mankind to
            heaven, but to save humanity from hell," he famously said.
        </p>

        <p class="chapter-text">
            This is what it means to be civilised: the rejection of double standards
            and the application of one standard for all. The real clash is not
            between civilisations, but between a civilised game and uncivilised
            chaos.
        </p>

        <p class="chapter-text">
            But he was no soulless bureaucrat. He was able to bear the weight of his
            role thanks to a deeply contemplative inner life – as one can read in his
            'Markings'. These writings reveal that, in his private life, he was
            devoted to God. His introspective, mystical Christianity did not provide
            him with an identity or a sense of belonging to a group. Instead, it
            gave him the strength to devote his life to something greater than
            himself.
        </p>

