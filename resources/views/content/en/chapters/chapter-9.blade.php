        <p class="chapter-lead">
            Many are drawn to the vision of a purely religious society. But this is
            an idea that risks corrupting faith through power and privilege.
        </p>

        <h2 class="chapter-heading">A Dangerous Dream</h2>

        <p class="chapter-text">
            "Christian nationalism" contains an inner contradiction: Christianity is
            about learning humility, while nationalism revolves around pride, which
            at times tips over into arrogance. It is not necessarily an impossible
            combination – but it does require that we avoid a particular trap.
        </p>

        <p class="chapter-text">
            Christian nationalism is the dream of a unified nation held together by
            a strong sense of identity and belonging. But the dream can turn into a
            nightmare when the power of the state is used to enforce the vision. At
            that point, it is no longer about community, but about excluding,
            reshaping, or even erasing whatever does not fit. Ruthlessness is built
            into the project. In that sense, Christian nationalism can become
            radically un-Christian.
        </p>

        <p class="chapter-text">
            Many imagine "the secular" as an atheistic project designed to push
            faith into the private sphere. But nothing could be more mistaken. In
            fact, the secular – paradoxical as it may sound – is a deeply Christian
            idea.
        </p>

        <p class="chapter-text">
            The historian Tom Holland describes in his book Dominion how it all
            began with Jesus' words: "Give to Caesar what belongs to Caesar, and to
            God what belongs to God." In that sentence lay the seed of a
            revolutionary idea.
        </p>

        <p class="chapter-text">
            In the ancient world, religion and politics were inseparable. There was
            no boundary, no distance, no neutral zone. The ruler ruled by divine
            authority, and the state existed by religious justification. Only slowly
            did Christians begin to realise that these spheres should not be merged
            but kept distinct. That insight was refined over centuries – through the
            medieval struggle between pope and emperor, through the Reformation's
            distinction between spiritual and worldly authority, and through
            Enlightenment ideas about freedom.
        </p>

        <p class="chapter-text">
            No one knows exactly where the line should be drawn, but the basic
            principle is clear: there must be a neutral zone where faith is not
            turned into a tool of power. The secular state creates breathing space
            that protects faith from being corrupted by political privilege.
        </p>

        <p class="chapter-text">
            This must not be confused with secularisation, the process by which
            religion gradually loses influence over society and daily life. The
            secular state is instead about how we manage the charged relationship
            between faith and power. Properly understood, it serves as an important
            safeguard against religious coercion – and just as importantly, against
            atheist coercion.
        </p>

        <h2 class="chapter-heading">Making Room for the Golden Rule</h2>

        <p class="chapter-text">
            That society is permeated by Christian values is not a problem in itself
            – and in many ways, it already is. Jesus sums up the law in the Golden
            Rule: "Whatever you wish that others would do to you, do also to them."
            This principle of reciprocity underpins much of our modern legal order.
        </p>

        <p class="chapter-text">
            Perhaps the most Christian thing we can do is to safeguard this so that
            it is not undermined by various religious supremacy projects. We must
            never use the resources and laws of the state to gain privileges over,
            for example, Jews, Muslims, or non-believers.
        </p>

        <p class="chapter-text">
            For when faith becomes a path to power and status, we sell our soul in
            a Faustian pact. The faith then turns into something bribed, twisted,
            and eventually hollow.
        </p>

        <p class="chapter-text">
            The secular state does not dilute faith – quite the opposite. It makes
            it possible for Christians to actually live as Christians in society and
            to practice the Golden Rule.
        </p>

        <p class="chapter-text">
            It is also about exercising moderation. National pride works much like
            alcohol. In small doses it can give courage and strengthen
            self-confidence. In larger doses, judgment becomes clouded. Eventually
            we grow harsh and aggressive. When this is mixed with religious
            self-righteousness, it creates a toxic cocktail that hardens the heart
            and destroys judgement.
        </p>

        <x-quotecard>national pride works like…</x-quotecard>

        <p class="chapter-text">
            Of course, there is a healthy form of national pride grounded in
            moderation. Such pride and identity should rest on the idea of a secular
            and neutral state. This is an ancient heritage, shaped in many ways by
            Christian tradition, and one we ought to preserve and develop. It
            protects not only strangers and minorities. It also protects us
            Christians – especially from ourselves.
        </p>

        <h2 class="chapter-heading">Who Cheers for the Referee?</h2>

        <p class="chapter-text">
            One of the healthier expressions of nationalism and tribalism is
            football. In this game people rally behind their own team. There are
            passionate chants, songs, and cheering. But who cheers for the referee?
            Nobody. He moves through the game as a thankless, almost invisible
            presence – whistling, pointing, occasionally ruining your joy. He is
            often the subject of mockery and resentment. If he does a good job, you
            soon forget he existed. He inspires no devotion.
        </p>

        <p class="chapter-text">
            And yet, without him, there is no game. Nobody thinks, 'He is doing a
            poor job, therefore we don't really need a referee.' Remove the referee,
            and the game grows more violent. Before long, it doesn't even resemble
            football anymore.
        </p>

        <p class="chapter-text">
            The team is the object of devotion. But behind all the passion lies the
            realisation that the game is entirely dependent on someone that nobody
            loves: the referee. We all understand this instinctively, even if we
            have never put it into words.
        </p>

        <p class="chapter-text">
            We also understand that there must be someone that appoints the referee.
            There must be a national football association setting the rules,
            appointing officials, and handling appeals. And above that, international
            bodies like FIFA. These organisations are faceless, bureaucratic,
            occasionally corrupt – and absolutely necessary. Despite all the scandals
            surrounding FIFA, nobody seriously suggests that it should not exist. The
            question is never whether we need governing bodies, only whether they are
            doing their job honestly.
        </p>

        <p class="chapter-text">
            The football world is, in miniature, a model of something much larger.
            Every functioning society has referees – regulators, courts,
            international bodies. They are unglamorous by design. Their role is not
            to play the game but to make the game possible. The better they do their
            job, the less we notice them.
        </p>

        <p class="chapter-text">
            Rules, laws and procedures rarely inspire much enthusiasm. They interrupt
            us, restrain us, slow us down. They say 'no' when we want 'yes', and
            'wait' when we want 'now.' Just like the referee standing in the middle
            of the match, they are unpopular and often the target of discontent. We
            notice them most when they disappoint us.
        </p>

        <p class="chapter-text">
            Remote and faceless institutions are seldom the object of affection. Who
            sings hymns about the World Trade Organization? Who writes poems about
            the International Atomic Energy Agency?
        </p>

        <p class="chapter-text">
            And yet, without neutral institutions, there is no life as we know it.
            They are what make freedom possible in practice. The ability to trust a
            total stranger – to sign a contract, board a plane, drink the water,
            accept a banknote – is a quiet miracle. We do not think much about it.
            But we should.
        </p>

        <p class="chapter-text">
            If these institutions are removed, what remains is not freedom, but chaos
            and violence. States and institutions are becoming increasingly like
            mafias.
        </p>

