        <p class="chapter-text">
            Populists are like football fans that have taken their hatred of the
            referee to another level. All these faceless, bureaucratic and, at times,
            corrupt institutions should be torn down. It never dawns to them that
            they destroy the game they love.
        </p>

        <p class="chapter-text">
            We don't have particularly warm feelings towards these institutions –
            they're not something we 'like'. What is needed is a different kind of
            love: care, responsibility and vigilance. They must be staffed by honest
            and competent people. Every functioning society depends on their quiet
            work, even when it is hard to understand and far removed from our daily
            lives.
        </p>

        <p class="chapter-text">
            All this is an expansion of the commitment to the secular state and the
            Golden Rule. Impartiality and reciprocity mean that the same standards
            apply – the powerful and the weak alike. A rules-based order is a
            trust-based one. It creates an invisible bond that holds the world
            together.
        </p>

