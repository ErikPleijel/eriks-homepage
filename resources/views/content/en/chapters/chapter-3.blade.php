        <h2 class="chapter-heading">A Spiritual Vaccine</h2>

        <p class="chapter-text">
            The symbols and mysteries of Christianity may seem strange and ancient.
            Many disdainfully dismiss them as 'old dogma'. We need to understand how
            powerful symbols can be. They touch on a deeper level and can be much
            stronger than words and concepts. The image of the cross has influenced
            our entire culture. It was a turning point in history.<x-footnote-trigger
                label="1"
                text="Read, for example, Dominion: How the Christian Revolution Remade the World, by Tom Holland." />
        </p>

        <p class="chapter-text">
            Before Christ, the cross was purely a symbol of power. It was nothing
            more than a brutal instrument of execution. It represented the Roman
            Empire's ability to enslave people by intimidating them into submission.
        </p>

        <p class="chapter-text">
            After Christ, the cross became the exact opposite: a symbol of love.
            These are radical images. It is a story of God's compassion for the
            powerless. God is not somewhere far away, watching the misery of the
            world. Through Christ he comes down to our earthly and human level.
            The cross means that he suffers with his creation. The resurrection
            means that he still loves it and wants to forgive it. He does not
            destroy his enemies with fire and brimstone, but overcomes the desire
            for revenge.
        </p>

        <p class="chapter-text">
            Many people believe in God because he has power and can bring happiness
            and success. They say that they love him, but in reality it is the gifts
            and the power that they love. The story of the cross transforms everyone
            who believes in it and understands it. Power and the exercise of power
            cease to be fascinating. The worship of God is not the same as the
            worship of power.
        </p>

        <h2 class="chapter-heading">The Choice: Control or Trust</h2>

        <p class="chapter-text">
            In the light of this, it becomes clear how naive it is to use faith as
            a lever to control God. There is an enormous difference between these
            two statements:
        </p>

        <ul>
            <li>"My faith in Christ entitles me to a place in paradise".</li>
            <li>"The message of Christ gives me the courage to trust that God is good".</li>
        </ul>

        <p class="chapter-text">
            Is salvation within or beyond human control? If we want to feel in
            control, we must be firmly convinced that we have the right theology
            and the right piety. This creates an enormous fear of being wrong. The
            result is a strong belief in one's own infallibility. But when everything
            is out of our control, there is nothing to do but trust in God Himself.
        </p>

