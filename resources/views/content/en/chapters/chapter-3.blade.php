        <h2 class="chapter-heading">No Animal Training for Humans</h2>

        <p class="chapter-text">
            For some, it is more logical that having the right faith should pay
            off. They believe in Jesus in order to receive certain exclusive
            benefits. "What else is the point of being a Christian?" they ask.
        </p>

        <p class="chapter-text">
            Isn't it better to follow him because you really want to? True faith
            is voluntary and needs no reward. Rewards and punishments can be used
            to train animals. A human being should develop in a different way: as
            a free and responsible person with inner integrity.
        </p>

        <p class="chapter-text">
            The most important things in life are compassion, good judgement,
            insight, humility, truthfulness, responsibility and integrity, etc.
            These things cannot be bought or forced. They can only be cultivated
            in a free relationship. This can become a reality when we believe that
            God's love is a care for the health of the soul.
        </p>

        <p class="chapter-text">
            God and man are united in a common project – this is the meaning of
            the expression "following Christ".
        </p>


