        <h2 class="chapter-heading">A Liberating Leap of Faith</h2>

        <p class="chapter-text">
            According to the tradition of the Reformation, any attempt to bribe God
            is futile. The belief that you can control your own destiny is an
            illusion. This is actually a happy message. We do not need to believe
            out of fear, vanity or the need to control. Instead, it opens the way
            for a voluntary faith that touches us in the right way.
        </p>

        <p class="chapter-text">
            Bonhoeffer wrote:
        </p>

        <blockquote class="chapter-blockquote">
            When you have completely given up trying to make yourself into something
            special – be it a saint or a converted sinner or a churchman, a
            righteous or unrighteous, sick or healthy [...] then you throw yourself
            completely into the arms of God [...] and that, I believe, is faith,
            is repentance, and that is how you become a human being, a
            Christian.<x-footnote-trigger
                label="2"
                text="Full quote: &quot;I discovered later, and I'm still discovering right up to this moment, that it is only by living completely in this world that one learns to have faith. When you have completely given up trying to make yourself into something special – be it a saint or a converted sinner or a churchman, a righteous or unrighteous, sick or healthy – by this worldliness I mean living unreservedly in life's duties, problems, successes and failures – then you throw yourself completely into the arms of God, then you no longer take your own sufferings, but God's sufferings in the world seriously, then you watch with Christ in Gethsemane; and that, I believe, is faith, is repentance, and that is how you become a human being, a Christian.&quot; (Letters and Papers from Prison.)" />
        </blockquote>

        <p class="chapter-text">
            This exercise in trust makes some people nervous and confused. They
            think, "Surely there must be something you can point out to show that
            you are not on the same level as the unbelievers? Otherwise, you are
            just like everyone else! And isn't it risky to be totally dependent on
            God's will? Then you have no control at all!"
        </p>

        <p class="chapter-text">
            Why this need to compare your faith with others? Why this desire to
            make grace a right? If you don't want to let go, the adventure can
            never begin.
        </p>

        <p class="chapter-text">
            Faith is not a magic formula that makes God kind and friendly. Nor is
            faith about reaching a certain state of mind to get a ticket to heaven.
            Faith is about throwing yourself into God's arms and trusting that he
            is good and wants to help you. Only then is it possible to focus on
            what really matters. And only then is it possible to take God and the
            Christian faith really seriously.
        </p>

