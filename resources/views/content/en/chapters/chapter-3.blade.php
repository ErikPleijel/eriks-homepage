        <h2 class="chapter-heading">Follow This Young Man</h2>

        <p class="chapter-text">
            To call yourself a Christian requires two things: to be baptised and
            to follow a young man who walked on earth two thousand years ago. Some
            of his words and actions may be difficult to understand because he
            lived in a different time and culture. But it is clear that he had
            courage and a very strong sense of purpose.
        </p>

        <p class="chapter-text">
            It is said that he lived in the desert for forty days to demonstrate
            his ability to resist temptation. The devil offered him all the kingdoms
            of the world if he would worship him. He refused the bargain and instead
            chose the path of voluntary humility and self-sacrifice. From that
            moment on, he preached, performed miracles, and healed the sick.
        </p>

        <p class="chapter-text">
            To the horror of the moralists, he sought out all kinds of outcasts and
            ate and drank with them. He tried to explain that God loves sinners as
            well as the righteous. So we must do the same and show respect to those
            who do not deserve it, especially our enemies. We must not treat our
            enemies as they treat us.
        </p>

        <p class="chapter-text">
            He warned against the religious pedants who fixate on unimportant things
            and impose heavy burdens on people. Why don't you judge for yourselves
            what is right? he asked. All the commandments are based on the love of
            God and the Golden Rule. He once amazed his disciples by washing their
            feet, showing that he who serves is greater than he who rules.
        </p>

        <p class="chapter-text">
            One day, he said, all people will have their lives evaluated. Those who
            have been harsh and unforgiving towards others will then be measured by
            the same standard and find that they deserve a harsh and unforgiving
            punishment. On that day it will become clear that the most important
            thing is to care for those who are less fortunate, especially the poor.
            What particularly angers God, he said, is when you call another person
            a fool.
        </p>

        <p class="chapter-text">
            When he called his disciples, he did not offer them a life of comfort
            and prosperity. On the contrary, he said they could expect no rewards
            or favours. Nor can those who follow him expect to be in total control
            of their lives or to know where they are going. He said that they were
            participants in a great historical drama of which no one, not even he,
            had a complete overview.
        </p>

        <p class="chapter-text">
            Trust in the Heavenly Father was what he wanted to teach them. Only
            through me, he said, will you find such faith and freedom. This is the
            way, the truth and the life.
        </p>

        <p class="chapter-text">
            These were the things this young man taught. Following in his footsteps
            means first of all learning not to hate and despise other people.
        </p>

