        <h2 class="chapter-heading">Evolution Cannot Forbid Evil</h2>

        <p class="chapter-text">
            Secular humanism is a widespread outlook in the Western world. It reacts
            strongly against intolerance and ignorance. While it retains important
            elements of Christianity's moral heritage, it rejects belief in God. In
            this way, it distances itself from religion while seeking to preserve
            some of its ethical insights.
        </p>

        <p class="chapter-text">
            Can a humanistic worldview be built without any divine guiding
            principles? Can life be understood without a mysterious X-factor behind
            the true, the good, and the beautiful? Does it make sense to believe in
            both a purposeless universe and a humanistic ethic?
        </p>

        <p class="chapter-text">
            Secular humanists think so, and they have arguments to defend that
            position. Darwinian evolution undeniably explains a great deal about how
            life has developed. At first glance, the harsh struggle for survival
            seems tailor-made to produce selfishness – perhaps even psychopathy. But
            it doesn't have to be that way. Empathy, cooperation, even self-sacrifice
            can help a group hold together – and outcompete its rivals.
        </p>

        <p class="chapter-text">
            Let's imagine a scenario: Tribe P, the psychopaths, is plagued by
            infighting due to their lack of empathy and emotional intelligence. Tribe
            C, the cooperators, emerges through a mutation. Its members are more
            empathic – and better at working together. In the struggle for survival,
            C prevails in the long run, while P, despite its aggressiveness,
            perishes. The meek inherit the earth.
        </p>

        <p class="chapter-text">
            Evolution is brutal and often outright genocidal, and yet, it may still
            turn the tables. Such explanations are suggestive, but perhaps not
            conclusive. A possible story is not always the true one. I will leave
            the verdict to the scientists.
        </p>

        <p class="chapter-text">
            The main point here is something different. Evolution does not give us
            any guidance about right and wrong. It produces both the impulse to care
            for a child and the impulse to fear the stranger. It cannot tell us which
            ones to follow – and which ones to hold back. Should we accept a logic
            that says it is good if some people are wiped out, or reject it? Should
            we accept or reject the idea that every human being has intrinsic value,
            regardless of strength, intelligence, or usefulness?
        </p>

        <p class="chapter-text">
            You can argue it either way. Darwinism offers no guidance on what we
            ought to do. It points no finger. It gives no directions.
        </p>

        <h2 class="chapter-heading">Humanism with a Negative Compass</h2>

        <p class="chapter-text">
            There is, however, a way for secular humanism to form a powerful ethical
            compass. It is built on shock – a deep emotional reaction to evil. The
            horrors of the twentieth century have seared themselves into our
            collective memory. Images of extermination, gulags, and mass graves still
            grip us. The revulsion is intense, the conviction firm – almost religious
            in its force.
        </p>

        <p class="chapter-text">
            It's often said that, in a strange way, Hitler has replaced Christ as the
            moral reference point of our age. He functions as a negative ideal, an
            "anti-model" against which we orient ourselves. Earlier generations
            looked to saints, but nowadays we define ourselves by what we reject. The
            compass no longer points toward the good, but away from evil.
        </p>

        <p class="chapter-text">
            Secular humanism knows what to resist and despise, but finds it harder to
            articulate a positive centre. It is pushed away from evil more than
            pulled towards the good. The "Christ compass" is replaced by an
            "anti-Antichrist" compass.
        </p>

        <p class="chapter-text">
            This is not the whole truth. Secular humanism does articulate a positive
            core – affirming human potential, celebrating reason, compassion, and the
            common good. The question is whether good intentions are enough. It is
            easy to love humanity as an abstraction. It is harder to love the
            particular, often irritating person in front of you.
        </p>

        <p class="chapter-text">
            The stakes are high. Secular humanism is the dominant outlook in the
            West. But if its higher ideals fail to inspire, it risks sliding toward
            the dark side of evolution and the "survival of the strongest". Tribal
            instincts can take over, displacing the commitment to the common good.
        </p>

        <p class="chapter-text">
            The ultimate question is why care at all, if the universe is indifferent.
            Earlier ages had an answer. They trusted in a Christ-like compass that
            pointed toward something beyond themselves. To understand what we may
            have lost, we need to see how that compass was formed.
        </p>

        <h2 class="chapter-heading">Borrowed Moral Capital</h2>

        <p class="chapter-text">
            In Dominion, Tom Holland notes that in the ancient world, power, strength,
            and domination were admired virtues. Compassion for the weak was not
            regarded as a moral ideal. Christianity overturned that hierarchy entirely.
        </p>

        <p class="chapter-text">
            The Cross is at the centre of that revolution. Crucifixion was not merely
            execution; it was a public declaration of humiliation. It was a way of
            saying: this person is utterly worthless. Christianity placed that very
            symbol at the heart of its faith. Voluntary suffering, self-giving love,
            and mercy toward enemies became a new kind of greatness. The central
            symbol of Roman domination was turned upside down.
        </p>

        <p class="chapter-text">
            This reversal was summed up in one of Jesus' most unsettling sayings:
            "The last shall be first, and the first shall be last."
        </p>

        <p class="chapter-text">
            To the Roman mind, this was a moral obscenity. We see this allergic
            reaction preserved in the writings of Christianity's earliest critics.
            The philosopher Celsus, writing in the second century, mocked the very
            idea that the crucified Christ could be divine. He asked with dripping
            sarcasm:
        </p>

        <blockquote class="chapter-blockquote">
            What did he do that was noble or godlike? Did he strike back in kind when
            he himself was despised and mocked? If he was a god, why did he not
            display his divine power by punishing or avenging those who wronged him
            – or his Father?
        </blockquote>

        <p class="chapter-text">
            The Church Father Origen, responding to these charges, argued that Jesus's
            true divine power was revealed precisely in his endurance of suffering for
            the sake of others – and in his refusal to mock back. To the Roman
            sensibility, this was preposterous. This is not what a god does.
        </p>

        <p class="chapter-text">
            In the ancient world, honour was found in the ability to crush and
            humiliate those who opposed you. A god who did not strike back – who did
            not mock his mockers – was no god at all.
        </p>

        <p class="chapter-text">
            The crucifixion of Christ, his resurrection, and his divinity – these are
            the ingredients of a faith that caused a "Big Bang." It sent out cultural
            shockwaves that turned brute power into sin and humility into strength.
        </p>

        <p class="chapter-text">
            The move is so bizarre that one might wonder whether humans could have
            invented it at all. The Bible suggests something else: it presents it as
            a "divine folly" that outsmarts human reason and undermines the
            foundations of the ancient worldview.
        </p>

        <p class="chapter-text">
            Holland did not arrive at this insight as a defender of faith. He began
            as a secular historian and found himself, almost reluctantly, recognising
            Christianity's moral imprint across Western culture. Beliefs many now
            take for granted – the equal value of every human being, concern for the
            weak, suspicion of unchecked power – are not timeless or self-evident.
            They are products of history.
        </p>

        <p class="chapter-text">
            If a modern humanist had tried to explain to a Roman that his slaves
            possessed inherent dignity and equal moral worth, the response would
            likely have been: No, they don't.
        </p>

        <p class="chapter-text">
            This raises an awkward question for secular humanism. Some of its deepest
            moral reflexes were shaped by Christianity. What keeps those reflexes
            alive once the roots have been severed? Will they fade or eventually be
            forgotten? Can they prevail under the pressures of resurgent tribalism?
        </p>

        <p class="chapter-text">
            Secular humanists believe they can. But Friedrich Nietzsche –
            Christianity's most formidable critic – argued, and even hoped, that they
            were mistaken. In short, Nietzsche thought that once God was gone,
            morality would lose its binding force. Compassion, equality, and concern
            for the weak would gradually lose their hold. People would come to realise
            that they had no compelling reason to be kind, no deep justification for
            treating all lives as equal. Nietzsche did not foresee a calm or rational
            humanism. He foresaw a crisis: either a descent into nihilism, where
            nothing truly matters, or the rise of the "Übermensch" who would restore
            older, pagan ideals of strength and dominance.
        </p>

        <p class="chapter-text">
            Holland reaches a similar conclusion – though he regards its implications
            with alarm rather than hope. He likens the transition to a multi-stage
            rocket. Christianity provided the massive "first stages" – the theological
            fuel of a God-man on a cross – that gave the Western world enough thrust
            to escape the gravity of ancient cruelty. Today, secular humanism has
            jettisoned those stages as empty shells. It views itself as a space probe,
            gliding through the universe on pure reason alone.
        </p>

        <p class="chapter-text">
            Holland's fear is that without that initial propellant, the probe will
            eventually lose its way – or crash, spectacularly.
        </p>

