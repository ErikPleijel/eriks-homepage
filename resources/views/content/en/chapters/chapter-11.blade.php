        <h2 class="chapter-heading">Navigation Without a North Star</h2>

        <p class="chapter-text">
            Can a moral compass remain reliable if it lacks a positive, Christ-like
            centre? What are the consequences of losing the "propellant" once
            provided by living traditions? What happens when the mystical and
            otherworldly are stripped away?
        </p>

        <p class="chapter-text">
            One risk becomes immediately apparent: a purely negative compass can
            create negative driving forces such as anger and fear. The fear of
            repeating a catastrophe – the slogan "never again" – creates an
            atmosphere of anxiety, outrage and vigilance. Public debate quickly
            escalates into moral disgust. Contempt, rather than love, becomes the
            driving force. We risk mirroring the very hatred we seek to oppose.
        </p>

        <x-quotecard
            id="chapter-11"
            text="If we resist evil with hatred, we become what&nbsp;we&nbsp;oppose.
We need more than outrage — we need a faith that draws us toward&nbsp;the&nbsp;good."
            align="center"
            lang="en"
