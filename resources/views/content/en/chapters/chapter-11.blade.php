        <p class="chapter-text">
            Another risk is equally evident: human beings long to belong to something
            larger than themselves. The real question is not "faith or no faith," but
            rather, "faith in what?" Rejecting a belief in God leaves a void – and
            voids attract substitutes for religion. All too often, that space is
            filled by extremism and tribal rage.
        </p>

        <p class="chapter-text">
            There is another problem: secular humanism can offer sound ethical
            arguments, but arguments alone rarely motivate. They are a weak defence
            against the temptations of power and corruption. Why obey the law or
            follow the rules if breaking them is more profitable – and you can get
            away with it? Secular humanism may not provide a strong enough motive to
            refrain from cheating when no one is watching.
        </p>

        <p class="chapter-text">
            Disillusionment takes hold and gains momentum. Everything is reduced to a
            power game, where everyone is seen as pursuing their own advantage. The
            question is no longer "what is true?" but "who benefits?" Everyone is
            assumed to be corrupt, and everyone to be lying. Values are dismissed as
            a façade – something maintained for appearance rather than conviction. In
            the end, nothing really matters.
        </p>

        <p class="chapter-text">
            This descent into cynicism easily follows when a secular worldview runs
            out of fuel. For a time, belief in human progress can sustain hope and
            energy. But such faith is fragile, and tends to fracture under the
            pressure of history.
        </p>

        <p class="chapter-text">
            If there is no divine standard or Platonic ideals, then humanity becomes
            its own highest authority. "Man is the measure of all things." But which
            humanity? In practice, this is often interpreted as: "the average Joe"
            is the measure of all things. If you are better than most, you are a
            good person. The result: a self-satisfied, comparison-driven culture.
            The old command – 'know thyself' – is replaced by 'rank thyself.'
        </p>

        <p class="chapter-text">
            Everyone has blind spots. We all conceal things from ourselves – failures,
            transgressions and impulses we would rather not name. When we have no
            awareness that such blind spots exist, it becomes easy to assume that our
            own perspective is clear, our judgments sound, and our culture is at the
            peak of humanity. You don't see the difference between moral rage and
            tribal rage. "We are good people, so we should rule the world." This
            sense of civilisational superiority opens the door for grave crimes.
        </p>

        <p class="chapter-text">
            In secular humanism, there is nothing that transcends humanity. Its
            popular creed – be true to yourself – forms a circular trap. The ego
            makes a poor judge of itself. Blind spots are not side effects but part
            of the design.
        </p>

        <p class="chapter-text">
            It is tempting to blame religion for the world's conflicts. But secular
            humanism – the dominant moral outlook of modernity – may prove to be more
            part of the problem than the solution.
        </p>

