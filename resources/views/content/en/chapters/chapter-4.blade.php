            When faith discourages growth, we become easy prey to tyranny.
            There are seductive voices that say we don't need to grow up.
        </p>

        <h2 class="chapter-heading">Are Humans Badly Programmed Robots?</h2>

        <p class="chapter-text">
            Some have a very pessimistic view of man. They think that human
            beings are totally depraved and that they can never improve. A
            dark view of man makes the light of grace shine brighter, they believe.
        </p>

        <p class="chapter-text">
            But if man is a hopeless case, it is not his fault that he is
            the way he is. Then he is like an animal who cannot be held responsible
            for his actions. By denying responsibility, sin is eliminated. A wrongly
            programmed robot has no need to ask for forgiveness. This is hardly what
            the Christian belief in grace is about.
        </p>

        <p class="chapter-text">
            Recognising your helplessness, powerlessness and inability is one thing.
            Recognising your sins is quite another. It is important not to confuse
            the two. Suppose someone prays like this: "Alas, I am a slave to my
            desires and cannot do right!" That is not a confession of sin. It is
            not a sin to be helpless. A helpless person should ask for help, not
            forgiveness.
        </p>

        <p class="chapter-text">
            The embarrassing thing is that we sometimes make mistakes when we are
            perfectly capable of doing the right thing. Often it is not difficult
            at all, and many times it is entirely our own fault. A prayer for
            forgiveness is only meaningful if you take responsibility for your
            mistakes. Some people take the easy way out. Instead of admitting their
            mistakes (which can be quite painful), they admit their inability and
            powerlessness (which is often a pleasant exercise in self-pity).
        </p>

        <p class="chapter-text">
            If you are helpless, ask for help – not forgiveness. If you have done
            wrong, seek forgiveness – not excuses. This distinction is key to
            growth, because it sharpens our understanding of what is truly our
            responsibility.
        </p>

