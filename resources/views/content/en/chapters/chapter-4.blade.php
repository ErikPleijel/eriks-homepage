        <p class="chapter-text">
            Yet the language of human agency and freedom sometimes provokes anxiety
            in certain churches. Critics argue that it may encourage people to take
            credit for their good deeds – and thus fall into pride. In response,
            they construct a theology that emphasises human weakness and the absence
            of free will.
        </p>

        <p class="chapter-text">
            The idea is that by emphasising human weakness, you deepen dependence
            on a powerful God. But the same move can also create a longing for
            powerful authoritarian leaders.
        </p>

        <p class="chapter-text">
            When people are taught that they cannot govern themselves, they may
            soon begin looking for someone else to do it for them.
        </p>

        <h2 class="chapter-heading">A Dangerous Derailment</h2>

        <p class="chapter-text">
            There are two traps to avoid. The first is to believe that you must be
            strong, virtuous and successful in order to be accepted and 'saved'.
            This is conditional love. It leads to stress and performance anxiety
            because you can never know if you are good enough.
        </p>

        <p class="chapter-text">
            The second trap is that you have to be weak, failed and generally
            miserable to be accepted. This is also conditional love. It frightens
            people into passivity and prevents them from growing and developing.
            "Strength comes, strength wanes. God's love eternally remains" – that
            is true unconditional love!
        </p>

        <p class="chapter-text">
            For some, the gospel – the good news – is that God expects nothing of
            us human beings. Nothing good can come from us, so self-confidence is
            sinful pride. Trusting in God and using reason become two things that
            do not go together. Personal development becomes a dangerous thing.
        </p>

        <p class="chapter-text">
            Is this the worship of an unloving God? True love ought to inspire, not
            oppress. Rather than being a source of stress, high expectations can be
            uplifting. Trust in God can be combined with healthy self-confidence.
        </p>

        <p class="chapter-text">
            We humans are created with the ability to develop good qualities such
            as reason and empathy. Paradoxically, this is what makes us sinners.
            The problem is that we don't develop and use these abilities as much as
            we should. Or we use them in the wrong way.
        </p>

        <p class="chapter-text">
            What happens when people are frightened into passivity and do not dare
            to mature and develop? What is the consequence when faith is only about
            resting in a nice and cosy feeling? It feels liberating to avoid
            personal responsibility. It feels good to avoid repentance. It feels
            peaceful to rest in the pleasant illusion of being humble.
        </p>

        <p class="chapter-text">
            People turn to God for peace of mind and tranquillity of heart. Many
            want it to be as easy as pressing a button. They do not understand that
            they have to deal with inner chaos and create inner order.
        </p>

        <p class="chapter-text">
            'Cheap grace is grace without discipleship,' Bonhoeffer told Lutherans
            in Germany in the 1930s. 'Cheap grace is the mortal enemy of our
            church.' To his horror, he saw how easy it was for the Nazis to
            manipulate them. He believed that the only way for them to wake up from
            their moral complacency was a heightened sense of ultimate responsibility
            before God.<x-footnote-trigger
                label="1"
                text="From the book The Cost of Discipleship by Dietrich Bonhoeffer." />
        </p>

        <p class="chapter-text">
            Those who downplay the importance of following Christ cannot build
            resilience against the Faustian bargain.
        </p>

