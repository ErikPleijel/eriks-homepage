        <h2 class="chapter-heading">The Serpent and The Grand Inquisitor</h2>

        <p class="chapter-text">
            Man's big problem is original sin: the pleasure of doing bad things.
            Typical examples are bullying, revenge fantasies, schadenfreude, mockery
            – and the intoxicating lust for power. Evil acts are not perceived as
            evil because they are pleasurable. It is easy to like evil. Is there
            anyone who is completely free of it?
        </p>

        <p class="chapter-text">
            A cunning serpent whispers in your ear: "All your pleasant feelings are
            good feelings!" He wants to convince us that this is not a problem at
            all. We are good just the way we are. We should affirm our inner nature
            and not pretend. "What feels natural must be right," it whispers. "This
            is the same as being authentic and true to yourself!"
        </p>

        <p class="chapter-text">
            The Grand Inquisitor is a legend told in Dostoyevsky's novel The
            Brothers Karamazov. It is about a meeting between Christ and an old
            inquisitor who organised the burning of heretics in Spain in the 16th
            century.
        </p>

        <p class="chapter-text">
            The Inquisitor accused Christ of not heeding the 'wise and terrible
            spirit' he had encountered in the desert. This, the Inquisitor declared,
            was a grave mistake. He should not have resisted the temptations of
            power.
        </p>

        <p class="chapter-text">
            The Inquisitor hurled accusations against Christ. The evil spirit, he
            declared, possessed a deeper understanding of human nature. Voluntarily
            choosing good is beyond human capacity. People do not want freedom, he
            argued. They crave bread, spectacle and authority. They long to be told
            what to think, what to believe and how to live. They crave a grand,
            theatrical display of power. The Church, he insisted, must adapt the
            Christian faith to people's weaknesses.
        </p>

        <p class="chapter-text">
            You have inflicted a cruel and unnecessary burden upon humanity, the
            Inquisitor explained to Christ. A life of voluntary discipleship is too
            difficult for most people. We, the Church, must correct your error, he
            said. We must be merciful to people and lift this heavy burden of
            freedom, this agonising responsibility. We need to give them what they
            want most of all: security.
        </p>

        <p class="chapter-text">
            Throughout this impassioned tirade, Christ watched the Inquisitor
            without saying a word. When the Inquisitor finally finished, he expected
            a response, an argument, a refutation. Instead, Christ approached him
            slowly and, to the old man's utter astonishment, kissed him gently on
            his lips. The Inquisitor shuddered. "Go," he said, "go, and return no
            more... do not come again... never, never!"
        </p>
