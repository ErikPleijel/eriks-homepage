        <p class="chapter-lead">
            Ancient thinkers knew this: Democracies can easily be hijacked by
            tyrannical demagogues who seduce people with lies and delusions. There
            is a way out of the dark cave.
        </p>

        <h2 class="chapter-heading">Your Library Is Your Paradise</h2>

        <p class="chapter-text">
            The Renaissance humanist Erasmus of Rotterdam was a champion of
            classical education. He understood that man is full of faults, but he
            had hope that he could improve. Through grace he could become God's
            co-creator. He praised compassion, humility and moderation. "Your
            library is your paradise," he said.
        </p>

        <p class="chapter-text">
            Martin Luther's friend and colleague Philipp Melanchthon followed the
            same line. He too recognised the importance of education. Melanchthon
            combined the principles of the Reformation with the classical heritage
            from ancient Greece and Rome. This laid the foundation for a rich
            intellectual tradition.
        </p>

        <p class="chapter-text">
            Unfortunately, many Protestants today seem to have neglected this. Now
            is the time to rediscover and revitalise our educational heritage! (The
            Germans call this Bildung; this is a broad term that emphasises personal
            growth).
        </p>

