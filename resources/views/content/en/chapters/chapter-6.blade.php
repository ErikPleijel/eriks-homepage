        <h2 class="chapter-heading">The Path Out of the Shadowlands</h2>

        <p class="chapter-text">
            Plato can be a friend in need. In The Republic, he admittedly put
            forward some undemocratic and rather absurd ideas. It was shocking to
            his contemporaries in Athens that he used the collectivism of Sparta as
            a model. His student Aristotle thought he went too far.
        </p>

        <p class="chapter-text">
            At the same time, The Republic can be surprisingly progressive. Socrates
            described an ideal society ruled by educated and noble leaders. Plato's
            own brother Glaucon listened and was deeply impressed:
        </p>

        <blockquote class="chapter-blockquote">
            – What wonderful figures you have created in these ruling men, Socrates!
            he said. Like a real sculptor! – Ruling women too, Glaucon! I replied.
            You must not think that what I have said applies more to men than to
            women …<x-footnote-trigger
                label="4"
                text="The Republic 540d." />
        </blockquote>

        <p class="chapter-text">
            Wise women must be educated to rule society as philosopher-queens,
            thought Plato. In Athens in 400 BC, this was a revolutionary idea.
        </p>

        <p class="chapter-text">
            There are both good and bad political ideas in The Republic. But what
            is perhaps most relevant to us today is not its politics, but its
            psychology and virtue ethics.
        </p>

        <p class="chapter-text">
            Plato divided the soul into three parts. The first is reason and the
            second is desires and drives. The third can be described by words such
            as combativeness, ambition, passion and spiritedness.
        </p>

        <p class="chapter-text">
            Chaos arises when, for example, desire begins to rule over reason. Or
            when the urge for action becomes an unbridled desire for power. Plato
            argued that there must be no internal civil war between the three parts.
            He wrote:
        </p>

        <blockquote class="chapter-blockquote">
            Rather, the individual should create real order in his own house, become
            his own master, guardian and friend, and reconcile the three parts just
            like three notes in a scale ... he will live in good terms with himself.
            In that condition he can act …<x-footnote-trigger
                label="5"
                text="The Republic 443d." />
        </blockquote>

        <p class="chapter-text">
            Inner health is about the creation of inner harmony. According to Plato,
            the three parts of the soul (reason, will and desire) must be coordinated
            so that each has its proper place and function.
        </p>

        <p class="chapter-text">
            A disorganised soul, he believed, was bound by its desires and
            fixations. It is like being chained up in a dark cave, lost in shadows
            and illusions. 'The Form of the Good' is like the sun that makes
            everything appear in its proper light.
        </p>

        <p class="chapter-text">
            The way out of the cave, according to Plato, is through education and
            virtue. He reflected on what would later be called the four cardinal
            virtues. Today we could interpret them in the following way:
        </p>

        <ul>
            <li>Wisdom, e.g. knowing what you don't know.</li>
            <li>Just-mindedness, e.g. to separate the issue from the person.</li>
            <li>Moderation, e.g. not going to extremes.</li>
            <li>Courage, e.g. not letting fear rule over reason.</li>
        </ul>

        <p class="chapter-text">
            Plato saw these virtues as safeguards against inner chaos and political
            decay. His warning still stands: if citizens lose the capacity for
            self-government, they will soon look for someone else to govern them.
        </p>

        <p class="chapter-text">
            That said, we need not follow his dream of a perfect rational system run
            by devoted experts. The dream survives – many have tried to implement
            it, with varying success – but it carries a fatal flaw: it suppresses
            human passion, and so cripples human beings.
        </p>

        <p class="chapter-text">
            Better to embrace democracy as the messy, imperfect game it is – one
            that works surprisingly well, provided people respect the rules of the
            game.
        </p>

        <p class="chapter-text">
            Today, democracy and the rule of law are challenged by the oldest
            temptation of all – the one Plato already saw consuming Athens: might
            is right.
        </p>

        <p class="chapter-text">
            The cities of ancient Greece are long gone, yet their story speaks more
            clearly than ever. Their world has vanished, but the appetite for power
            and the struggle for justice are ever new.
        </p>

