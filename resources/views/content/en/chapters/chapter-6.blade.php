        <h2 class="chapter-heading">A Precious Treasure Not to Be Lost</h2>

        <p class="chapter-text">
            A good place to start is with the ancient Greeks. There are those who
            reject ancient Greek democracy because it excluded women and slaves.
            Certainly there was no small amount of elitism, sexism, racism and
            fascism in the ancient world. Neither the people of the Bible nor the
            classical philosophers were free from such things.
        </p>

        <p class="chapter-text">
            But the dark background need not discourage us. It is precisely the
            liberation from the might-is-right philosophy that is important. Both
            successes and failures can be relevant to us.
        </p>

        <p class="chapter-text">
            Since these people lived in a completely different culture, we can try
            to see "similarities between different things and differences between
            similar things". In this way we can search for the principles that
            underlie the art of being human.
        </p>

        <x-content-image
            src="/images/chapters/chapter-6/athens_edited1.JPG"
            alt="The School of Athens, Raphael"
            caption="The Athenian study group."
            width="90" />

        <p class="chapter-text">
            The accumulated experience of humanity is a precious treasure, and to
            forget it is not very progressive. To move forward, we must learn from
            history. To be ignorant of what occurred before you were born is to
            remain always a child, said Cicero.
        </p>

        <h2 class="chapter-heading">A Revolutionary Idea: The Quest for the Best</h2>

        <p class="chapter-text">
            There was a turning point in Greek history – perhaps sometime around
            600–500 BC – when the Greeks stopped asking, "What pleases the ruler?"
            and instead began to ask: "What is best – and why?"
        </p>

        <p class="chapter-text">
            They were moving away from a world of obedience, where subjects existed
            mainly to serve the king, and where the highest "virtue" was submission.
            This was the birth of a society in which human beings were expected to
            think – to argue, to deliberate, to search for truth. Not merely to
            obey or to please, but to dare to ask what is best. Instead of standing
            silently before a throne, the citizen stood up in the marketplace and
            spoke.
        </p>

        <p class="chapter-text">
            Once people begin to ask "what is best?", a new world opens up. It
            becomes fertile ground for great cultural achievements: philosophy,
            drama, political thought, art and architecture, science and mathematics.
        </p>

        <p class="chapter-text">
            That shift changed everything, and it still matters today. This is the
            kind of freedom the Greeks were prepared to defend with their lives. In
            our own time, we may have to defend it again, as leaders increasingly
            want citizens to please them rather than question them. They build courts
            of fear and flattery, and in doing so, they suffocate the quest for the
            best.
        </p>

        <p class="chapter-text">
            The Greeks did put a great deal of effort into pleasing the gods
            (remember Euthyphro in a previous chapter), so they were not entirely
            free. But even with that limitation, it was an extraordinary achievement
            of human creativity.
        </p>

        <p class="chapter-text">
            Of course, it was never obvious what "the best" really was, or who
            should decide it. Those were precisely the questions the Greeks wrestled
            with. Understanding their struggle makes us better prepared to defend
            freedom – which should never be taken for granted.
        </p>

        <h2 class="chapter-heading">The Search for the Best Regime</h2>

        <p class="chapter-text">
            It is quite impressive that ancient Athens had a system of government
            that was as inclusive as it actually was. They developed a democracy
            that did not rely on bureaucrats and professional politicians. Instead,
            they relied on the participation of citizens and their ability to use
            their common sense.
        </p>

        <p class="chapter-text">
            Athens was at odds with Sparta, which practised a much more brutal form
            of slavery. They could not understand why Athens gave power to the
            uneducated masses. Instead, their system of government was an oligarchy,
            the rule of the few.
        </p>

        <p class="chapter-text">
            The Spartans worshipped the god Phobos (fear) for his ability to create
            discipline and uniformity. Their society was marked by extreme
            collectivism and their culture was – Spartan. The Athenians also had
            their follies, but they had a rich and flourishing culture.
        </p>

        <p class="chapter-text">
            Athens and Sparta represented two contrasting political ideals and
            clashed for influence among the Greek city-states. These two ideas are
            still in conflict.
        </p>

        <p class="chapter-text">
            The first idea is that all citizens are basically equal and should have
            a say in the affairs of society. Everyone has common sense, and if they
            don't, they can develop it over time through engagement in society and
            equal dialogue. If people are treated as adults, it is possible that
            they will actually become adults.
        </p>

        <p class="chapter-text">
            The second idea is that people are not equal. Humanity can be divided
            into "the clever and enlightened" and "the stupid and unenlightened".
            To create a good society, the former must have power over the latter.
        </p>

        <p class="chapter-text">
            Plato lived in Athens, but he followed the second line. He wanted to
            introduce aristocracy – "the rule of the best", that is, rule by a
            noble and cultured elite. He was highly critical of democracy, which he
            believed gave free rein to passions and irrationality. Sooner or later,
            he argued, it would degenerate into tyranny. The great challenge for
            democracy in our time is to prove Plato wrong on this point.
        </p>

        <x-content-image
            src="/images/chapters/chapter-6/Cicero.JPG"
            alt="Cicero"
            align="right"
            width="30" />

        <p class="chapter-text">
            We should remember, however, that what we now call democracy is more a
            legacy of ancient Rome than of Athens. Cicero lived in the Roman
            Republic, which had a kind of mixed form of government: monarchy,
            aristocracy and democracy. The rule of one leader can create capacity
            for action. Rule by the wisest can provide expertise. Rule by the common
            people can promote common sense. Cicero believed that a well-designed
            constitution takes the best from each.<x-footnote-trigger
                label="1"
                text="See On Politics, Alan Ryan, 4. Roman Insights: Polybius and Cicero." />
        </p>

        <h2 class="chapter-heading">We Can Learn From Plato</h2>

        <p class="chapter-text">
            In a chapter of the book Adventures and Reflections, I describe my work
            on water supply in Nepal during the Maoist insurgency. There I argue for
            Karl Popper's idea of the open society. Popper is known for his harsh
            criticism of Plato. Too harsh, I think. Even those of us who believe in
            democracy can learn from Plato's ideas.
        </p>

        <p class="chapter-text">
            Plato's scepticism about democracy can be hard to accept. However, it
            becomes more understandable when we realise that he was mainly
            criticising what we call populism. Today we are painfully aware of how
            easily democracies can fall into darkness. Populism can create a cult of
            power that leads to incompetent leadership, authoritarianism and tyranny.
        </p>

        <p class="chapter-text">
            We can also relate to Plato's famous parable of the cave. We're
            constantly bombarded with unreliable information from all sides. It's
            hard to separate fact from fiction. Fake news spreads quickly, and many
            people get caught up in distorted views of the world. It feels like
            we're living in a world of shadows, confusing illusion with reality. We
            long for real knowledge and a true understanding of the world.
        </p>

        <h2 class="chapter-heading">The Illusion of Freedom in the Shadowlands</h2>

        <p class="chapter-text"><strong>Is this freedom?</strong></p>

        <ul>
            <li>To say whatever comes to mind without inhibition.</li>
            <li>To follow your impulses and do whatever you want.</li>
            <li>To give vent to your overinflated ego.</li>
            <li>To let go of the reins of your inner tyrant.</li>
        </ul>

        <p class="chapter-text">
            Isn't true freedom about being liberated from such narrow and
            self-centred horizons? The Swedish philosopher and educator Alf Ahlberg
            believed that a functioning democracy requires us to develop the ability
            to think freely. The problem is not a lack of freedom of expression, but
            our tendency to enslave ourselves.
        </p>

        <p class="chapter-text">
            Ahlberg argued that we humans are very good at convincing ourselves that
            we are more rational than we really are. We defend our opinions with
            reasons that seem logical but are really based on desires, sympathies and
            antipathies. We see facts that flatter and please us, and turn a blind
            eye to what humiliates and angers us. We easily fall prey to social
            prejudices and superstitions. He wrote:
        </p>

        <blockquote class="chapter-blockquote">
            To think freely is not to blindly follow all the lines of thought that
            our inclinations and interests, our self-love and our infantile
            megalomania mark out. To become a spiritually free person is to become
            free from this limitation of the horizon.<x-footnote-trigger
                label="2"
                text="Alf Ahlberg, Tankelivets frigörelse." />
        </blockquote>

        <p class="chapter-text">
            In our narcissistic culture, free thinking is synonymous with following
            your innermost feelings. Freedom means inflating your ego and giving it
            free rein. Your subjective opinion becomes something sacred that should
            not be violated. True is what feels right. Good is what feels good.
        </p>

        <p class="chapter-text">
            Socrates was adamant that these are not the same thing. Such subjectivism
            makes us easy prey for those who are skilled at manipulating
            emotions.<x-footnote-trigger
                label="3"
                text="See, for example, Socrates&apos; discussion with Polos and Callicles in Plato&apos;s dialogue Gorgias." />
        </p>

        <p class="chapter-text">
            Ahlberg argued that fascism and other totalitarian ideologies are based
            on primitive subjectivism. Collective feelings of flattery, hatred and
            revenge are believed to be good because they are pleasurable.
        </p>

