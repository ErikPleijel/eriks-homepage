        <h2 class="chapter-heading">Three Parts of the Anchor</h2>

        <p class="chapter-text">
            When talk of human dignity is not grounded in anything, it easily becomes
            a castle in the air, which disappears at the slightest strain. The belief
            can be anchored in the biblical idea that we are created in the image of
            God. Human love of science and music is a sign of a deep inner link with
            the Creator.
        </p>

        <p class="chapter-text">
            When the belief that all human beings are created in the image of God
            becomes a fundamental part of who we are, we instinctively reject
            humiliation and dehumanisation. It creates a unifying bond. Dividing
            identities (like nationality) becomes less important than this unifying
            identity. It becomes natural to think that the Golden Rule and principles
            of justice apply equally to all.
        </p>

        <p class="chapter-text">
            Add to this another statement of faith. The Cross and Resurrection is a
            story of how Christ suffers and dies at the hands of the powerful, yet
            survives and triumphs. It is a complete reversal of conventional logic
            and worldly principles. Instead of 'survival of the strongest', it is
            about 'survival of the gentle-hearted'.
        </p>

        <p class="chapter-text">
            Many in our society are attracted to the idea of unbridled power. What
            begins as an admiration for strength and toughness can turn into
            callousness and brutality. The ability not to care becomes a source of
            pride. Heartlessness becomes a virtue, gentleness a weakness. Such an
            attitude fits perfectly with fascist ideology, which glorifies uninhibited
            cruelty and sees it as something to be admired.
        </p>

