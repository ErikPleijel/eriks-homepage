        <h2 class="chapter-heading">Does the Universe Care?</h2>

        <p class="chapter-text">
            A purely materialist universe has no place for meaning. We are nothing
            but accidental byproducts of evolution. We may care for each other, since
            that is how the survival game has wired us. But the nature that created
            us does not care. Soon, everything will be forgotten: everything we have
            done, everything we have valued. Our values are a brief flicker in an
            infinite dark.
        </p>

        <p class="chapter-text">
            This loneliness may in the end drain our energy. We may still think that
            feeding the hungry is right – but in a universe that does not care, why
            keep going when it gets hard? The Golden Rule is simple in theory. In
            practice, it demands that we override our own ego and act against our
            immediate self-interest.
        </p>

        <p class="chapter-text">
            Why care at all in an indifferent universe? Every moral act becomes a
            defence against a vacuum. Without a source beyond ourselves, we slowly
            deplete our inner reserves. In the end we may collapse inward – like a
            star that runs out of fuel and folds into a black hole.
        </p>

        <p class="chapter-text">
            Many sense that we need God precisely here – as the ultimate guarantor
            of values, the one who makes morality objective and real. But a simple
            divine command is not enough. If God says 'you must care,' we might ask:
            why? If the answer is simply that he can crush us like ants if we don't,
            then might is right. And a morality built on that foundation leads
            straight to cruelty.
        </p>

        <p class="chapter-text">
            Nor do we need a perfect ethical system – a complete set of instructions
            with answers to every moral dilemma. What we lack is not instructions
            but fuel – the motivation to keep the Golden Rule when it is hard, when
            it is costly, when no one is watching. We need a bulwark against the
            voice that whispers: nothing really matters.
        </p>

        <p class="chapter-text">
            What I mean by positive faith is this: the conviction that ultimate
            reality itself cares – that what we do matters, finally and truly. How
            could we be careless and irresponsible if we have faith in a God who
            cares? A faith like that functions as the sun – an inexhaustible source
            of energy and stamina for the long work of caring.
        </p>

        <p class="chapter-text">
            What are the grounds for such a conviction? There are forces that pull
            us beyond what is visible: the true, the good, and the beautiful. And
            then there is the "divine folly", which – as we saw in the previous
            chapter – once burst into history like an earthquake, sending shockwaves
            still felt today.
        </p>

        <p class="chapter-text">
            These are signs. If ultimate reality is personal rather than impersonal
            – friendly rather than indifferent – then a trusting faith is not wishful
            thinking. It is a recognition of what is truly there.
        </p>

        <p class="chapter-text">
            Let us now turn to a positive faith that has shaped human hearts for
            millennia and still offers a firm anchor in a stormy world. It is not a
            faith of favour and privilege, but of care and responsibility. The
            ultimate guarantor of values is not a God of raw power – but a God of
            love. That is the personal life-force that strengthens the soul and
            upholds human dignity.
        </p>

