        <p class="chapter-text">
            Faith in the Cross is a protection against this fall into darkness. It
            leads to a transformation of the heart and challenges the common
            perception that strength lies in the ability to dominate. The fascination
            with power disappears. Gentleness, not heartlessness, is the true value
            and source of strength. Even though self-defence can be justified, it is
            only a necessary evil. Callousness and aggressiveness must never become
            a pleasure or a way of life.
        </p>

        <p class="chapter-text">
            To believe in the Cross is to have faith that good will triumph over
            evil. It is a symbol of protection against a world where might is right
            and ruthlessness is the key to success. Believers gain the courage to
            refuse to take part in games that corrupt the soul. It is better to
            suffer injustice than to do injustice.
        </p>

        <p class="chapter-text">
            The mystery of the Cross carries a deeper significance: it is a sacrifice
            made to save sinners. There is ultimate responsibility, but also ultimate
            forgiveness. Measured against a perfect divine standard, we do not pass
            – and we know it.
        </p>

        <p class="chapter-text">
            That awareness changes everything. Knowing your own shortcomings before
            God produces humility, not superiority. And it adds something the secular
            outlook cannot supply: a divine perspective on the human blind spots. You
            never fully know what God knows about yourself – let alone others. You
            are not in a position to render final verdicts. The judgment seat is not
            yours. Such faith renders comparison and virtue signalling beside the
            point.
        </p>

        <p class="chapter-text">
            This is a God who does not watch us from afar, but who comes down to
            earth and suffers alongside us. When we look at the sufferer, we are
            looking at the divine. This faith will not let us look away or lose
            heart: A God who suffers in the world, but cares about it anyway – a sun
            that shines on the righteous and the unrighteous alike.
        </p>

        <p class="chapter-text">
            Now combine these two beliefs with a third: God as a spiritual guide out
            of the shadowlands. A trusting faith opens the heart to the possibility
            of being wrong and frees us from the chains of stubborn certainty. It
            becomes possible to hold opposing ideas in mind and to integrate. It no
            longer matters so much if one's worldview is shaken from time to time.
            There remains a steady pull toward truth and the good, toward the
            widening of horizons. We step out of the cave's shadowed life and into
            the world of sunlight.
        </p>

