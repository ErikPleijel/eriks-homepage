            In the Christian tradition, God is known as Creator, Saviour, and Helper
            – the divine Trinity. United, they stand as a formidable force against
            what destroys and for what is good. Faith in God is like an anchor that
            keeps you from drifting away with the current. With faith, you're harder
            to buy – less like a puppet on strings, less easily bent by flattery or
            fear. You learn to drive your own chariot. With trusting faith, your soul
            is not for sale.
        </p>
