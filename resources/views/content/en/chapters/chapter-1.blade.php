        <h2 class="chapter-heading">A Terrifying Sight</h2>

        <p class="chapter-text">
            What is the worst possible consequence of polarisation? Once, a long
            time ago, I saw it with my own eyes.
        </p>

        <p class="chapter-text">
            In the first chapter of my book, <em>Adventures and Reflections</em>, I
            recount the experiences I had while travelling in Rwanda during the
            genocide of 1994. I describe a journey with a rebel officer who showed a
            place where people had been killed. Walking among the corpses is a
            harrowing experience, I can assure you.
        </p>

        <p class="chapter-text">
            The chapter ends with a reflection on God, evil and the meaning of life.
        </p>

        <h2 class="chapter-heading">I Visit a Prison and Meet the Killers</h2>

        <p class="chapter-text">
            In the second chapter of the book, I recount my visit to a Rwandan
            prison, where the prisoners guilty of the atrocities were held. It was
            two years after the genocide. The prison was absolutely overcrowded,
            with many more prisoners than it was built to hold.
        </p>

        <p class="chapter-text">
            One of them showed me around and I spoke to some of them. To my
            surprise, they were quite friendly. In the prison yard I saw a group of
            prisoners singing while one of them conducted. After a while I realised
            that it was the church choir rehearsing for the Sunday Mass.
        </p>

        <p class="chapter-text">
            This puzzled me. Were these really the men who had committed such
            terrible atrocities? Why were they such easy prey to evil?
        </p>

        <h2 class="chapter-heading">The Path to The Dark Side</h2>

        <p class="chapter-text">
            A Rwandan man who had participated in the killings, but felt deep
            remorse, said in a newspaper interview: “You were praised and respected
            when you killed Tutsis. You were proud. We were all brainwashed to think
            that if we didn’t kill the Tutsis, they would kill us.”<x-footnote-trigger
                label="1"
                text="From an article in the Swedish newspaper Svenska Dagbladet, &quot;Hon fick två svenskar dömda för folkmord,&quot; by Gunilla von Hall." />
        </p>

        <p class="chapter-text">
            Flattery and fear, in other words. Without an inner defence against
            toxic passions, something begins to break. Integrity cracks – and the
            soul goes up for sale. Clever demagogues and agitators seize the
            opportunity to manipulate and control. They have no use for qualities
            such as compassion and good judgment.
        </p>

        <x-content-image
            src="/images/chapters/chapter-1/compass_ff.png"
            alt="Compass pointing to Fear and Flattery, and not Compassion and Wisdom."
            caption="Fear and flattery can disrupt the inner compass and lead us in the wrong direction." />

        <p class="chapter-text">
            I can only speculate about what the prisoners thought and believed, but
            I suspect that they were never taught that this is ‘the path to the dark
            side’. Fear, anger, hatred, suffering – at some point you have to be able
            to break the chain of evil. Isn’t that what the churches should be
            teaching young people?
        </p>

        <p class="chapter-text">
            The visit to the prison astonished me. How could such seemingly nice and
            kind people become so cruel and brutal in certain situations? Was this a
            collection of extremely sick individuals? Or were they human beings, not
            very different from us?
        </p>

        <p class="chapter-text">
            I think they are more like us than we would like to admit. The problem
            of fear, anger and hatred concerns us too. Who is completely immune to
            the great driving force of evil – the intoxicating sense of power? Does
            anyone have an inner compass that works perfectly?
        </p>

        <p class="chapter-text">
            A misaligned compass can create hell on earth and lead to a spiritual
            prison. We need to be guided more by wisdom and compassion. Not only the
            body but also the soul needs medicine and exercise. Everyone needs to
            build inner health and resilience.
        </p>

        <h2 class="chapter-heading">What makes us human?</h2>

        {{-- Float right so the paragraph immediately following wraps around it. --}}
        <x-content-image
            align="right"
            src="/images/chapters/chapter-1/prst_hund.png"
            alt="A man feeds a dog." />

        <p class="chapter-text">
            There is a fundamental difference between training an animal – like a
            dog – and nurturing the growth of a human being. A dog may be unruly at
            first, but with rewards and punishment, it can be moulded into
            well-behaved conduct. It learns to wag its tail at familiar faces and to
            bark at strangers.
        </p>

        <p class="chapter-text">
            Unlike animals, human beings are capable of moral development – shaped by
            reason and compassion rather than by rewards and punishments. Integrity
            cannot be imposed from without; it must be nurtured from within.
        </p>

        <p class="chapter-text">
            But what happens if we raise a child as if it were a dog? At best, the
            child may become well-behaved – but without a genuine sense of right and
            wrong. It may learn to “bark” at strangers simply because they are
            different. It will lack an inner moral compass.
        </p>

        <h2 class="chapter-heading">Good and Evil</h2>

        <p class="chapter-text">
            Is the struggle between good and evil an external or internal conflict?
            The frightening reality is that charismatic psychopaths can rise to
            become leaders of nations. These individuals believe that might is right
            and they can seduce millions of people into blindly following them.
            Hitler is a case in point. When this happens, there is an external
            conflict. Then we must resist firmly, resolutely and fearlessly.
        </p>

        <p class="chapter-text">
            But if we see the drama of the world only as a struggle between ‘us’ and
            ‘them’ – us who are good against them who are evil – we walk on a
            dangerous path. When we think we are morally superior to other people,
            we may feel entitled to humiliate them and put them down. If we call
            others ‘monsters’, we risk becoming ‘monsters’ ourselves. We can become
            so blind that we do not see the contradiction. That is when we lose our
            moral compass.
        </p>

        <p class="chapter-text">
            The battle between good and evil is not only something that happens out
            there, in the world, or between nations. It is something that plays out
            inside each of us.
        </p>
