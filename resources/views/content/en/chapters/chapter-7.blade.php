        <h2 class="chapter-heading">Hope When Everything Seems to Go Wrong</h2>

        <p class="chapter-text">
            The Christian belief in life after death can lead to a pious escape
            from the world, but it doesn't have to be that way. On the contrary,
            it can become a source of inner strength. Hope protects us from
            emptiness and cynicism.
        </p>

        <p class="chapter-text">
            Heavenly hope gives the feeling of having a home. It creates a natural
            stoicism that is not forced or contrived. Even if everything goes wrong,
            all is not lost. Hope becomes an inner source, which is less dependent
            on circumstances. The heavenly hope can therefore also create an earthly
            hope. Bonhoeffer wrote:
        </p>

        <blockquote class="chapter-blockquote">
            Optimism is in its essence not an opinion about the present situation
            but a living force, a power to hope where others give up, a power to
            hold one's head up high where everything seems to fail, a power to bear
            setbacks, a power that does not leave the future to the pessimist but
            claims it for hope. Admittedly, there is a cowardly and foolish optimism
            that should be discarded. But optimism as a desire for the future is
            not to be despised, even if it is wrong a hundred times over; it is the
            health of life, which the sick must not contaminate. There are people
            who consider it a lack of seriousness, Christians who consider it a lack
            of piety, to hope for a better earthly future and to prepare for it.
            They believe in chaos, disorder and catastrophe as the order of the day,
            and in resignation or pious escapism they shirk responsibility for the
            continuation of life, for restoration, for the next generation. It may
            be that the Day of Judgement comes tomorrow, in which case we should
            gladly let go of the work for the future, but not before.<x-footnote-trigger
                label="5"
                text="From &quot;Letters from prison&quot;." />
        </blockquote>
