            Authoritarian leaders gain popularity by appearing strong and tough.
            However, ancient Stoic philosophy reveals that they are deeply weak
            and fragile.
        </p>

        <h2 class="chapter-heading">The West Must Rediscover Its Compass</h2>

        <p class="chapter-text">
            The ideas of the Greeks were passed on to the Romans, who developed a
            philosophy called Stoicism. It is relevant to those who want to be
            grounded in reality and work for the common good. Stoic philosophy
            contains some ideas that belong to an outdated worldview, but also
            things that are timeless.
        </p>

        <p class="chapter-text">
            It is in many ways embedded in Christianity. Many of the Christian
            church fathers, and probably St Paul, were influenced by the Stoics.
            Luther, Melanchthon and Erasmus had one thing in common: all three
            liked the writings of Cicero, which contain many Stoic ideas.
        </p>

        <x-content-image
            src="/images/chapters/chapter-7/streckg_eng.png"
            alt="Jerusalem and Athens: Enlightenment, Reformation, Renaissance, Middle Ages, Christianity, Judaism, Stoicism, Greek philosophy"
            align="right"
            width="25" />

        <p class="chapter-text">
            Alf Ahlberg wrote: "The humanist tradition is based above all on a
            fusion of Stoicism and Christianity". He argued that the Western
            humanist tradition has been shaped by a number of religions, philosophies
            and epochs. It developed during the Middle Ages, the Renaissance, the
            Reformation and the Enlightenment. Its ancient roots lie in Judaism,
            Christianity, Greek philosophy and Stoicism. He wrote that this is
            "the great line of our culture, on whose existence and vitality its
            whole future depends".<x-footnote-trigger
                label="1"
                text="Quote from the Swedish book Humanismen." />
        </p>

        <p class="chapter-text">
            The synthesis of Stoicism and Christianity is the origin of a precious
            treasure: the belief in human rights. This idea has matured and developed
            throughout history. This was mainly done by Cicero in antiquity, and
            then by Christian philosophers such as Thomas Aquinas in the Middle Ages,
            and Locke and Kant in the Enlightenment.
        </p>

        <p class="chapter-text">
            There are times when the West seems to have completely lost its moral
            compass. That is when we need to reconnect with our historical and
            spiritual roots.
        </p>

        <h2 class="chapter-heading">The Courage to Be Gentle</h2>

        <p class="chapter-text">
            A common misconception is that stoicism leads to an emotionless, cold
            state of mind. Its essence is self-awareness and self-control – which
            is why CBT (Cognitive Behavioural Therapy) can be seen as its modern
            heir. Taken to extremes, stoicism hardens into callousness, but held
            in balance it builds inner health.
        </p>

        <p class="chapter-text">
            Some young men today see themselves as culture warriors on a mission to
            rescue Western civilisation from decadence. A common trait is that they
            mistake dialogue and empathy for weakness. The irony is that what is
            presented as strength is often just brittleness in disguise.
        </p>

        <p class="chapter-text">
            True strength needs no performance of toughness. Stoic discipline is
            not about suppression of feeling but its governance – recall Plato's
            image of the charioteer.
        </p>

        <p class="chapter-text">
            When stoicism meets Christian love, strength finds its proper direction.
            It gives rise to the courage to be gentle without fear – to listen, to
            remain open to argument, and to be assertive without becoming defensive.
            That is real strength.
        </p>

        <h2 class="chapter-heading">Training Exercises for the Soul</h2>

        <p class="chapter-text">
            The ancient Stoics were inspired by the philosophy of Socrates, which
            included the idea that it is better to suffer wrong than to do wrong.
            They saw him as an example of how to live in a stormy world with inner
            peace and without corrupting one's soul.
        </p>

        <p class="chapter-text">
            The Stoics believed that man's most precious possession was the
            integrity of the soul. This means that external things such as success
            and wealth are less valuable. This can be compared with Jesus' statement:
            "What does it profit a man if he gains the whole world but loses his
            soul?"
        </p>

        <p class="chapter-text">
            The Stoic Epictetus saw God as a primal force that creates an inner
            strength in the soul. The Serenity Prayer is rooted in his philosophy.
        </p>

        <div class="my-8 border-l-4 border-stone-300 pl-6">
            <p class="text-center text-sm font-bold uppercase tracking-wide text-stone-500">The Serenity Prayer</p>
            <p class="chapter-blockquote mt-3 border-0 pl-0">
                God, grant me the serenity to accept the things I cannot change,
                courage to change the things I can, and wisdom to know the
                difference.<x-footnote-trigger
                    label="2"
                    text="The Serenity Prayer was formulated by Reinhold Niebuhr in 1926." />
            </p>
        </div>

        <p class="chapter-text">
            Most of what happens in the world is beyond our control, but what we
            can control are our reactions, said Epictetus. We can control our
            opinions, desires and aversions. For a Stoic, it is important to
            maintain an inner peace of mind that is independent of external
            circumstances. You must not allow yourself to be enslaved by base
            thoughts.
        </p>

        <p class="chapter-text">
            "We have nothing to fear but fear itself", said Franklin D. Roosevelt.
            The ability to feel fear is, of course, absolutely vital. We live in a
            dangerous world and if we couldn't feel fear, we would put ourselves in
            dangerous situations unnecessarily. What Roosevelt warned against is a
            fear that impairs judgement. Dangers and threats must be assessed with
            a balanced mind that neither overreacts nor cowardly closes its eyes to
            reality. We must be careful that fear does not lead to delusion, tunnel
            vision or blindness.
        </p>

        <p class="chapter-text">
            The trials and tribulations of everyday life can become exercises in
            keeping your balance. Epictetus gave the following advice to someone
            visiting a bathhouse (in the Roman Empire around 100 AD):
        </p>

        <blockquote class="chapter-blockquote">
            For example, if you are going to take a bath, think about what happens
            in a bathhouse: people splash each other, push each other, fight and
            steal from each other. You will be safer if you say to yourself: "I am
            going to have a bath and at the same time maintain my natural
            equilibrium." You should do this before everything you do. If something
            disturbs you while you are bathing, you will have the idea that "I
            intended not only to bathe but also to maintain my natural balance, and
            I can't do that if I am disturbed by what is happening now".<x-footnote-trigger
                label="3"
                text="Epictetus, The Enchiridion, point 4." />
        </blockquote>

        <p class="chapter-text">
            According to Stoicism, it is important to be able to think negatively
            and try to imagine unpleasant surprises. This may sound like pessimism,
            but it is actually optimism. It's like saying to yourself: "Even if I
            encounter difficulties and setbacks, I have an inner readiness to deal
            with the problems".
        </p>

        <p class="chapter-text">
            For a Stoic, it is particularly important to reflect on one's mortality
            and to be grateful for the life one has. This puts the world into a
            truer perspective.
        </p>

        <p class="chapter-text">
            It is important, however, that these Stoic exercises are not overdone.
            It should not lead to emotional coldness, indifference and an inability
            to care. Feelings of frustration and anger can be important motivators.
            The main thing is that anger does not become a pleasure and an
            unrestrained habit.
        </p>

