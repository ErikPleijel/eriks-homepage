        <h2 class="chapter-heading">Power Does Not Always Corrupt</h2>

        <p class="chapter-text">
            It is often said that power corrupts and absolute power corrupts
            absolutely. However, it seems that there is one exception and that is
            the emperor and stoic Marcus Aurelius. He had unlimited power to do
            whatever he wanted, but he tried hard to resist the temptation.
        </p>

        <p class="chapter-text">
            A Stoic sets limits to himself and his actions – not everything that
            can be done should be done – and has enough self-discipline to respect
            those limits. Power does not provide freedom from the burden of
            following principles.
        </p>

        <p class="chapter-text">
            "Beware of being a tyrant," Marcus said to himself. "So be honest,
            good and upright, love justice, be pious, be kind and loving, and be
            steadfast in the discharge of duty."<x-footnote-trigger
                label="4"
                text="Marcus Aurelius, Meditations, Book VI." />
        </p>

        <p class="chapter-text">
            Those who have power over others must first have self-awareness and
            self-control. This philosophy of life is relevant to politicians, civil
            servants, police officers, managers and others whose duty it is to serve
            a higher ethic and the common good.
        </p>

        <p class="chapter-text">
            Why be honest if it is more profitable to be corrupt? This was Glaucon's
            challenging question in The Republic. Socrates' answer was illuminating.
            A healthy body has value in itself. The same is true of the soul. The
            soul of the unjust is sick, while the soul of the just is healthy. Inner
            health has an intrinsic value, even if it is not always "profitable".
        </p>

        <p class="chapter-text">
            Justice is the defence against the idea that might is right. There are
            those who say they are fighting for justice, when in fact they are
            fighting for power and revenge. This is what happens when one lacks
            inner justice. The struggle for justice is both an inner and an outer
            struggle.
        </p>

        <p class="chapter-text">
            Anger can be the driving force to fight against injustice, but what
            about hate? It is important to learn to distinguish between these
            emotions. Hate can cause suffering to others and it can seriously damage
            your own intellect. It is wise to make this a rule of life: Anger may
            be OK, but hate is not. It is a matter of setting limits for yourself
            and learning not to cross them.
        </p>

