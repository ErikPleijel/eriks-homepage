        <h2 class="chapter-heading">Avoid Stubborn Dogmatism and Lazy Relativism</h2>

        <p class="chapter-text">
            Dogmatists cannot be wrong. But neither can relativists, because in
            their minds there is no right or wrong. Both are unwilling to listen to
            counter-arguments. Neither understands that they are trapped in their
            intellectual fixations.
        </p>

        <p class="chapter-text">
            Socrates was able to engage people and encourage better thoughts and
            ideas. Know thyself! he said. Be aware of your own ignorance! Only then
            can we free ourselves from delusions. To become wiser, we must help each
            other through dialogue.<x-footnote-trigger
                label="1"
                text="Read Plato&apos;s Apology of Socrates!" />
        </p>

        <p class="chapter-text">
            This was not a dialogue between deaf people, where no one listens to
            the other's arguments. If the person who disagrees with me is right, I
            am the first to give in, Socrates said. He said, "Wherever the winds of
            discussion blow – there we must go". It is this kind of give and take
            dialogue that can provide the Platonic insight: truth is something that
            can be approached and that in some sense exists.<x-footnote-trigger
                label="2"
                text="Plato, The Republic." />
        </p>

        <p class="chapter-text">
            "I have the absolute Truth," says the dogmatist. "Your truth is not my
            truth," says the relativist. Neither seeks truth through dialogue with
            others. Trapped in their shells, they cannot change, grow and evolve.
        </p>

        <p class="chapter-text">
            But suppose there is an objective Truth that stands above our favourite
            private ideas. Then the possibility opens up that our opinions need to
            be corrected. Being guided by the Truth keeps us awake from both
            dogmatic and relativistic slumber. Those who do so can make an important
            contribution to our troubled world: spreading the art of dialogue and
            reasoning.
        </p>

        <h2 class="chapter-heading">Phronesis – An Old Virtue in Need of Revival</h2>

        <p class="chapter-text">
            An important key to integration is what Aristotle called phronesis,
            which can be translated as practical wisdom, common sense and good
            judgement. This is something that is developed through work and life
            experience and through education.
        </p>

        <p class="chapter-text">
            It is an intuitive form of knowledge that involves being able to discern
            what is essential and what is non-essential in any situation. It means
            being able to consider different courses of action in order to choose
            the best one.
        </p>

        <x-content-image
            src="/images/chapters/chapter-8/aristoteles_2.png"
            alt="Aristotle"
            caption="Aristotle"
            width="50" />

        <p class="chapter-text">
            In the Nicomachean Ethics, Aristotle developed a theory of how we can
            cultivate the emotions to harmonise with judgement. He emphasised the
            need for good habits. When we were children, our emotions were a little
            'raw' and not finely tuned to good judgement. We tended to enjoy doing
            bad and unwise things. We had an aversion to doing good and wise things.
            Emotional refinement requires consistent practice. When we create good
            habits, we feel pleasure when we do good things and pain when we do bad
            things.
        </p>

        <p class="chapter-text">
            This process is related to the cultivation of virtues, especially
            phronesis, which is related to moderation. In practical life we often
            need to find the golden mean and not go too far in either direction. It
            is important not to overreact or underreact.
        </p>

        <h2 class="chapter-heading">'Half-Educated Experts'</h2>

        <p class="chapter-text">
            Experts are indispensable because they are knowledgeable in a particular
            field. At the same time, they are inherently more ignorant of what lies
            outside that area. Like all human beings, they are limited.
        </p>

        <p class="chapter-text">
            But what if they lack knowledge of something important, namely knowledge
            of their ignorance? What if they have a weak and diffuse understanding
            of their limitations? If they lack self-awareness, they tend to
            overestimate themselves and make confident statements about things they
            do not understand. Since the domain of competence is like a small island
            in a large ocean, it is likely that they are often in uncharted waters
            without realising it.
        </p>

        <p class="chapter-text">
            It was this kind of "half-education" that Socrates thought he saw in the
            technicians and engineers of his time. He noticed that although they
            were very knowledgeable in their field ...
        </p>

        <blockquote class="chapter-blockquote">
            … because they were skilled in the practice of their art, each of them
            considered himself wise even in the highest things, and this misjudgement
            obscured the wisdom they actually had …<x-footnote-trigger
                label="3"
                text="Plato&apos;s Apology of Socrates." />
        </blockquote>

        <p class="chapter-text">
            A technocracy is a government of "half-educated" experts. Being an
            expert in one area can create a false sense of being an expert in
            everything.
        </p>

        <h2 class="chapter-heading">The Practical Relevance of Bildung</h2>

        <p class="chapter-text">
            Knowledge gaps are often filled with illusions of knowledge – prejudices,
            false assumptions, simplistic solutions. This is the logic of blind
            ignorance.
        </p>

        <p class="chapter-text">
            Humanities studies and Bildung cultivate an awareness of our own
            limitations. They foster a kind of enlightened ignorance – an
            understanding of what we do not know. That awareness strengthens our
            ability to navigate a world of change, ambiguity, and contradiction.
        </p>

        <p class="chapter-text">
            This is especially important for those in leadership. We need educated
            leaders who combine expertise with judgement. They may not always possess
            the right answers, but they know how to ask better questions – and how
            to approach problems through dialogue rather than knee-jerk reaction.
        </p>

        <p class="chapter-text">
            This ability develops as we learn to recognise the different dimensions
            of life – for example: safety and liberty, justice and mercy, the global
            and the local, faith and reason, individual and collective, tradition
            and progress.
        </p>

        <p class="chapter-text">
            These pairs of opposites often trigger tribal reflexes. We feel compelled
            to choose a side and defend it at all costs. Complexity is reduced to
            slogans. Nuance is sacrificed for belonging.
        </p>

        <p class="chapter-text">
            An integrated person resists that impulse. They can hold opposites
            together and live with ambiguity – without panic, and without declaring
            war.
        </p>

        <p class="chapter-text">
            But integration is difficult. We live in an age that offers a shortcut:
            leaving the thinking to computers.
        </p>

        <p class="chapter-text">
            With the rise of artificial intelligence, the expression 'selling one's
            soul' has taken on a new meaning. Humans have something AI lacks:
            phronesis. AI can have poor judgment because it has no lived experience,
            no sense of responsibility, no intuition of time and history. Every time
            we defer to AI on a decision we could reason through ourselves, we grow
            a little weaker. Human phronesis must be actively cultivated – because
            we, not our tools, are ultimately responsible for the world we make.
            Bildung is our existential defence – it is how we keep the servant from
            becoming the master.
        </p>

