        <p class="chapter-lead">
            When demagogues create division, we long for harmony. Perhaps this is a
            mistake. Perhaps we should strive for another ideal: the ability to hold
            a complex world together.
        </p>

        <h2 class="chapter-heading">Four Pitfalls When Attempting to Create Harmony</h2>

        <p class="chapter-text">
            How can people from different cultures live together in harmony? What
            should they do when they are strangers to each other and have very
            little in common with each other? Should they strive for unity or
            diversity? Do they want to live in equality, or does one group try to
            dominate? This leaves four possibilities.
        </p>

        <ol>
            <li class="mt-3"><strong>Mosaic: Equality and Diversity.</strong> In a mosaic, people from
            different cultures live side by side. They stick to their own traditions
            and do not change their language or ways of living and thinking. No one
            culture is considered superior to another. However, the harmony is
            easily disturbed and there is a constant fear of domination. In this
            situation you have to be politically correct and walk on eggshells so
            as not to offend anyone. Everything is linked to identity, pride and
            inferiority complex. The danger is that people get on each other's
            nerves.</li>

            <li class="mt-3"><strong>Segregation: Dominance and Diversity.</strong> In a segregated
            society, some groups of people float on top. It's like oil and water:
            they never mix, no matter how much you stir. An example of this is
            South Africa and Namibia under apartheid. Some people believe that it
            is a divine order that some people should have power and privilege. If
            others just submit, they can be accepted like children in a family.
            Social harmony, they say, should be based on submissive respect and
            patriarchal benevolence.</li>

            <li class="mt-3"><strong>Melting pot: Equality and Unity.</strong> A melting pot is created
            when all communities cut the roots of their respective traditions and
            see only what they have in common. It can easily absorb ideas and
            impressions from all corners of the world. It develops a cosmopolitan
            culture characterised by easily consumed food, art and music. The
            melting pot becomes fluid and unhistorical, shunning anything
            complicated. Those who cling to their traditions are seen as
            abnormal.</li>

            <li class="mt-3"><strong>Homogenisation: Dominance and Unity.</strong> People in a
            homogeneous group tend to think that what is common must be normal. And
            what is normal must be natural. And what is natural is probably a
            divinely ordained order. All deviant elements must be eliminated or
            transformed. Here it becomes admirable to be politically incorrect, and
            those who pick on minorities are seen as tough and strong. The most
            brutal forms of homogenisation are ethnic cleansing and genocide.</li>
        </ol>

        <p class="chapter-text">
            Which of these strategies should we choose? Notice that they all have
            one thing in common. They are different ways of avoiding what is foreign
            and different. The hope is to eventually reach a state of harmony, free
            from friction. But the result is often just the opposite.
        </p>

        <p class="chapter-text">
            Instead of seeking harmony at all costs, we could set a new ideal. We
            could aim for the ability to integrate.
        </p>

        <h2 class="chapter-heading">Humble Pride and Respectful Honesty</h2>

        <p class="chapter-text">
            It is important to learn how to deal with pride in our nation, tradition,
            faith, identity, and uniqueness. We should strive for a kind of 'humble
            pride'. We should be proud enough to look after our own communities and
            preserve our cultural heritage. And we should be humble enough not to
            look down on other people and to acknowledge the darker parts of our
            history.
        </p>

        <p class="chapter-text">
            We need to learn the art of reconciling opposites. What often causes
            polarisation is the struggle between the politically correct and the
            politically incorrect. These are two extremes to be avoided. Politically
            correct means being respectful but not sincere, which easily leads to
            hypocrisy. Politically incorrect means saying what you think without
            showing respect, which easily leads to bullying.
        </p>

        <p class="chapter-text">
            A well-integrated person can hold two thoughts in his mind at the same
            time. It is possible to be both truthful and considerate. The ability to
            solve this round square is essential for those who really want to
            communicate and be understood.
        </p>

        <p class="chapter-text">
            Brutal honesty without respect rarely leads to truth. More often, it
            leads to misunderstanding.
        </p>

