        <h1 class="chapter-title">Mognad: En listig orm viskar att vi inte behöver förändras</h1>

        <p class="chapter-lead">
            När tron avskräcker från mognad blir man ett lätt byte för tyranni.
            Det finns förföriska röster som säger att vi inte behöver växa upp.
        </p>

        <h2 class="chapter-heading">Är människor dåligt programmerade robotar?</h2>

        <p class="chapter-text">
            Det finns troende som har en mycket pessimistisk syn på människan.
            De tänker att hon är rakt igenom fördärvad och att hon aldrig kan
            förbättras. En mörk människosyn får nådens ljus att lysa klarare, tror de.
        </p>

        <p class="chapter-text">
            Men om människan är ett hopplöst fall, är det inte hennes fel att hon
            är som hon är. Då är hon som ett djur som inte kan ställas till svars
            för sina handlingar. Genom att förneka ansvaret trollar man bort synden.
            En felprogrammerad robot behöver ju inte be om förlåtelse. Detta är
            knappast vad den kristna tron på nåden handlar om.
        </p>

        <p class="chapter-text">
            Att erkänna sin hjälplöshet, vanmakt och oförmåga är en sak. Att
            erkänna sina synder är något helt annat. Det är viktigt att inte röra
            ihop det här. Säg att någon ber så här: "Ack, jag är slav under mina
            begär och kan inte göra rätt!". Detta är ingen syndabekännelse. Att
            vara hjälplös är ingen synd. Den som är hjälplös bör be om hjälp och
            inte om förlåtelse.
        </p>

        <p class="chapter-text">
            Det pinsamma är ju att man ibland gör fel, fastän man är fullt kapabel
            att göra rätt. Ofta är det egentligen inte alls svårt, och många gånger
            är det helt och hållet ens eget fel. En bön om förlåtelse är bara
            meningsfull om man tar ansvar för sina felsteg. En del väljer den lätta
            utvägen. I stället för att erkänna sina misstag (vilket kan vara rätt
            smärtsamt) erkänner de sin oförmåga och vanmakt (vilket ofta blir en
            behaglig övning i självömkan).
        </p>

        <p class="chapter-text">
            Om du är hjälplös, be om hjälp – inte om förlåtelse. Om du har gjort
            fel, sök förlåtelse – inte ursäkter. Att skilja mellan dessa två är en
            nyckel till mognad, eftersom det skärper blicken för vad som faktiskt
            är vårt ansvar.
        </p>

        <x-quotecard>En nyckel till mognad</x-quotecard>

        <p class="chapter-text">
            Men talet om mänsklig handlingsförmåga och frihet väcker ibland oro i
            vissa kyrkor. Man invänder att det kan leda till att människor tar åt
            sig äran för sina goda handlingar – och blir högmodiga. Därför formas
            i stället en teologi som betonar människans svaghet och hennes brist
            på fri vilja.
        </p>

        <p class="chapter-text">
            Tanken är att om man framhäver människans svaghet, fördjupas hennes
            beroende av en mäktig Gud. Men detta kan få oväntade följder: samma
            tankemönster kan också skapa en längtan efter mäktiga auktoritära ledare.
        </p>

        <p class="chapter-text">
            När människor får lära sig att de inte kan styra sig själva, börjar de
            snart leta efter någon som gör det åt dem.
        </p>

        <h2 class="chapter-heading">En farlig urspårning</h2>

        <p class="chapter-text">
            Det finns två fällor vi måste undvika. Det första är att tro att man
            måste vara stark, duktig och framgångsrik för att bli accepterad och
            "frälst". Detta är villkorlig kärlek. Det leder till stress och
            prestationsångest, för man kan aldrig veta om man är bra nog.
        </p>

        <p class="chapter-text">
            Den andra fällan är att man måste vara svag, misslyckad och allmänt
            miserabel för att bli accepterad. Även detta är villkorlig kärlek. Det
            skrämmer till passivitet och hindrar människor från att växa och
            utvecklas. "Styrkan kommer, styrkan går, Gud förbliver fader vår" –
            detta är sann ovillkorlig kärlek!
        </p>

        <p class="chapter-text">
            För vissa är evangeliet – det glada budskapet – att Gud inte förväntar
            sig något av oss människor. Inget gott kan komma från oss, så
            självförtroende är syndig stolthet. Att lita på Gud och att använda
            sitt förnuft blir två saker som inte går ihop. Personlig utveckling
            blir något farligt.
        </p>

        <p class="chapter-text">
            Är detta att tillbe en kärlekslös Gud? Äkta kärlek bör inspirera, inte
            trycka ned. I stället för att vara en källa till stress kan höga
            förväntningar vara upplyftande. Tillit till Gud kan kombineras med ett
            sunt självförtroende.
        </p>

        <p class="chapter-text">
            Vi människor är skapade med möjligheten att utveckla goda egenskaper,
            som förnuft och empati. Det är just detta – paradoxalt nog – som gör
            oss till syndare. För problemet är att vi inte utvecklar och använder
            dessa förmågor så mycket som vi borde. Eller så använder vi dem på ett
            felaktigt sätt.
        </p>

        <p class="chapter-text">
            Vad händer om folk skräms till passivitet och inte vågar mogna och
            utvecklas? Vad blir följden när tron enbart handlar om att vila i en
            skön och behaglig känsla? Det känns befriande att slippa personligt
            ansvar. Det känns bra att slippa bot och bättring. Det känns rofyllt
            att vila i den sköna illusionen att vara ödmjuk.
        </p>

        <p class="chapter-text">
            Människor vänder sig till Gud för att få frid i sinnet och ro i
            hjärtat. Många vill att detta ska vara lika enkelt som att trycka på
            en knapp. De förstår inte att de behöver hantera den inre oredan och
            skapa inre ordning.
        </p>

        <p class="chapter-text">
            "Billig nåd är nåd utan efterföljelse", sade Bonhoeffer till
            lutheranerna i Tyskland på 1930-talet. "Billig nåd är vår kyrkas
            dödsfiende." Till hans stora förskräckelse såg han hur lätt det var
            för nazisterna att manipulera dem. Han trodde att det enda sättet för
            dem att vakna upp ur sin moraliska självbelåtenhet var en förhöjd
            känsla av yttersta ansvar inför Gud.<x-footnote-trigger
                label="1"
                text="Ur boken Efterföljelse av Dietrich Bonhoeffer." />
        </p>

        <p class="chapter-text">
            De som tonar ned vikten av att följa Kristus kan inte bygga upp
            motståndskraft mot den faustiska pakten.
        </p>

        <h2 class="chapter-heading">Ormen och storinkvisitorn</h2>

        <p class="chapter-text">
            Människans stora problem är arvsynden: lusten att göra onda saker och
            olusten att göra goda saker. Typiska exempel är mobbning,
            hämndfantasier, skadeglädje, hånflin – och den berusande maktkänslan.
            Onda handlingar uppfattas inte som onda eftersom de känns behagliga.
            Det är lätt att gilla ondskan. Finns det någon som är helt fri från detta?
        </p>

        <p class="chapter-text">
            En listig orm viskar i örat: "Alla dina behagliga känslor är goda
            känslor!" Den vill övertyga oss om att detta inte alls är något
            problem. Vi är bra precis som vi är. Vi ska bejaka vår inre natur och
            inte förställa oss. "Det som känns naturligt måste vara rätt!", viskar
            den. "Detta är att vara autentisk och sann mot sig själv!"
        </p>

        <p class="chapter-text">
            Storinkvisitorn är en legend som berättas i Dostojevskijs roman
            Bröderna Karamazov. Den handlar om ett möte mellan Kristus och en
            gammal inkvisitor som anordnade kättarbål i Spanien på 1500-talet.
        </p>

        <p class="chapter-text">
            Storinkvisitorn anklagade Kristus för att han inte ville lyssna på den
            "kloke och fruktansvärde anden" i öknen den där gången. Detta,
            förklarade inkvisitorn, var ett allvarligt misstag. Han borde inte ha
            stått emot maktens frestelser.
        </p>

        <p class="chapter-text">
            Inkvisitorn slungade anklagelser mot Kristus. Den onda anden, förklarade
            han, hade en djupare förståelse för den mänskliga naturen. Att frivilligt
            välja det goda överstiger människornas kapacitet. Människor vill inte ha
            frihet, hävdade han. De vill ha bröd, skådespel och auktoritet. De
            längtar efter att få veta vad de ska tänka, vad de ska tro och hur de
            ska leva. De vill se storslagna spektakel och maktdemonstrationer.
            Kyrkan, menade han, måste anpassa den kristna tron till människors
            svagheter.
        </p>

        <p class="chapter-text">
            Du har ålagt mänskligheten en grym och onödig börda, förklarade
            inkvisitorn för Kristus. Ett liv i frivillig efterföljelse är för svårt
            för de flesta människor. Vi, kyrkan, måste rätta till ert misstag, sade
            han. Vi måste vara barmhärtiga mot människor och lätta på denna tunga
            frihetsbörda, detta plågsamma ansvar. Vi måste ge dem vad de helst av
            allt vill ha: säkerhet.
        </p>

        <p class="chapter-text">
            Under hela denna passionerade tirad betraktade Kristus inkvisitorn utan
            att säga ett ord. När inkvisitorn äntligen var färdig förväntade han
            sig ett svar, ett argument, en vederläggning. Istället närmade sig
            Kristus honom långsamt och – till den gamle mannens stora förvåning –
            kysste honom varsamt på läpparna. Inkvisitorn ryste till. "Gå", sade
            han, "gå och kom aldrig mer tillbaka... kom aldrig mer tillbaka...
            aldrig, aldrig!"
        </p>

