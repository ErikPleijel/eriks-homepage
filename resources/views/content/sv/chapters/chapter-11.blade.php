        <p class="chapter-lead">
            Sekulär humanism är passionerad i sitt motstånd mot ondska och okunnighet
            – men utan en levande tro på det goda riskerar kampen att drivas av
            rädsla och bli destruktiv.
        </p>

        <h2 class="chapter-heading">Evolutionen kan inte förbjuda ondskan</h2>

        <p class="chapter-text">
            Sekulär humanism är en utbredd världsbild i västvärlden. Den reagerar
            starkt mot intolerans och okunnighet. Samtidigt som den behåller viktiga
            delar av kristendomens moraliska arv avvisar den tron på Gud. På så sätt
            distanserar den sig från religionen samtidigt som den försöker bevara
            vissa av dess etiska insikter.
        </p>

        <p class="chapter-text">
            Är det möjligt att bygga en humanistisk världsbild helt utan gudomliga
            vägledande principer? Kan livet förstås utan en gåtfull X-faktor bakom
            det sanna, det goda och det sköna? Är det rimligt att tro på både ett
            meningslöst universum och en humanistisk etik?
        </p>

        <p class="chapter-text">
            Sekulära humanister anser det, och de har argument för att försvara den
            ståndpunkten. Darwins evolutionsteori kan onekligen förklara mycket om
            hur livet utvecklats. Vid första anblicken verkar den hårda kampen för
            överlevnad gynna själviskhet – kanske till och med psykopati. Men det
            behöver inte vara så. Empati, samarbete och till och med självuppoffring
            kan hjälpa en grupp att hålla ihop – och konkurrera ut sina rivaler.
        </p>

        <p class="chapter-text">
            Låt oss skapa ett scenario: Stam P, psykopaterna, plågas av interna
            stridigheter på grund av sin brist på empati och emotionell intelligens.
            Stam S, samarbetarna, uppstår genom en mutation. Dess medlemmar är mer
            empatiska – och bättre på att arbeta ihop. I kampen för överlevnad
            segrar S på lång sikt, medan P, trots sin aggressivitet, går under. De
            saktmodiga ärver jorden.
        </p>

        <p class="chapter-text">
            Evolutionen är skoningslös och ofta rentav folkmordisk, men likväl kan
            den vända på steken. Sådana förklaringar är intressanta, men kanske inte
            fullständiga. En möjlig förklaring är inte nödvändigtvis den rätta. Jag
            överlåter saken till forskarna.
        </p>

        <p class="chapter-text">
            Huvudpoängen här är något annat. Evolutionen ger oss ingen vägledning om
            rätt och fel. Den ger upphov både till instinkten att ta hand om ett barn
            och impulsen att frukta främlingen. Den kan inte tala om för oss vilka vi
            ska följa – och vilka vi ska hålla tillbaka. Ska vi acceptera en logik
            som säger att det är bra om vissa människor utrotas, eller ska vi avvisa
            den? Ska vi acceptera eller avvisa tanken att varje människa har ett
            inneboende värde, oavsett styrka, intelligens eller nytta?
        </p>

        <p class="chapter-text">
            Det går att argumentera för båda ståndpunkterna. Darwinismen ger ingen
            vägledning för hur vi bör handla. Den pekar inte ut någon riktning. Den
            ger ingen vägledning.
        </p>

        <h2 class="chapter-heading">Humanism med en negativ kompass</h2>

        <p class="chapter-text">
            Det finns dock ett sätt för sekulär humanism att utveckla en kraftfull
            etisk kompass. Den bygger på chock – en djup känslomässig reaktion inför
            ondskan. 1900-talets fasor har etsat sig fast i vårt kollektiva minne.
            Bilder av den absoluta ondskan har fortfarande ett starkt grepp om vårt
            moraliska medvetande. Avskyn är stark, övertygelsen fast – nästan
            religiös i sin kraft.
        </p>

        <p class="chapter-text">
            Det sägs ofta att Hitler, på ett paradoxalt sätt, har ersatt Kristus som
            vår tids moraliska referenspunkt. Han fungerar som ett negativt ideal, en
            "antimodell" som vi orienterar oss mot. Tidigare generationer såg upp
            till helgon, men idag definierar vi oss själva genom det vi förkastar.
            Kompassen pekar inte längre mot det goda, utan bort från det onda.
        </p>

        <p class="chapter-text">
            Den sekulära humanismen vet vad den ska motstå och förakta, men har
            svårare att formulera en positiv kärna. Den drivs bort från det onda mer
            än den dras mot det goda. "Kristus-kompassen" ersätts av en
            "anti-antikrist-kompass".
        </p>

        <p class="chapter-text">
            Detta är inte hela sanningen. Den sekulära humanismen formulerar faktiskt
            en positiv kärna: den bekräftar människans potential och hyllar förnuftet,
            medkänslan och det gemensamma bästa. Frågan är om goda avsikter räcker.
            Det är lätt att älska mänskligheten som en abstraktion. Det är svårare
            att älska de verkliga, ofta irriterande personerna man möter i
            vardagslivet.
        </p>

        <p class="chapter-text">
            Det är mycket som står på spel. Den sekulära humanismen är den
            dominerande världsåskådningen i västvärlden. Men om dess högre ideal
            misslyckas med att inspirera riskerar den att glida mot evolutionens
            mörka sida och "den starkastes överlevnad". Stamtänkande kan ta över och
            tränga undan engagemanget för det gemensamma bästa.
        </p>

        <p class="chapter-text">
            Den yttersta frågan är varför man ska bry sig överhuvudtaget, om
            universum är likgiltigt. Tidigare epoker hade ett svar. De litade på en
            Kristus-kompass som pekade mot något bortom dem själva. För att förstå
            vad vi kan ha förlorat behöver vi se hur den kompassen en gång blev till.
        </p>

        <h2 class="chapter-heading">Lånat moraliskt kapital</h2>

        <p class="chapter-text">
            I boken Dominion visar Tom Holland att den antika världen hyllade makt,
            styrka och dominans. Medkänsla med de svaga betraktades inte som ett
            ideal. Det kristendomen gjorde var att helt omkullkasta denna hierarki.
        </p>

        <p class="chapter-text">
            Korset står i centrum för denna revolution. Korsfästelse var inte bara
            ett sätt att avrätta, utan också en offentlig förnedring. Det var ett
            sätt att säga: denna person är fullständigt värdelös. Kristendomen
            placerade just denna symbol i centrum för sin tro. Frivilligt lidande,
            självuppoffrande kärlek och barmhärtighet mot fiender blev en ny form av
            storhet. Den centrala symbolen för romersk dominans vändes upp och ner.
        </p>

        <p class="chapter-text">
            Denna värdeomkastning sammanfattades i ett av Jesu mest omvälvande ord:
            "De sista ska bli först, och de första sist."
        </p>

        <p class="chapter-text">
            För det romerska sinnet var detta en moralisk skymf. Vi ser denna
            "allergiska reaktion" bevarad i skrifterna från kristendomens tidigaste
            kritiker. Filosofen Celsus, som skrev på 100-talet, hånade tanken att
            den korsfäste Kristus kunde vara gudomlig. Med drypande sarkasm frågade
            han:
        </p>

        <blockquote class="chapter-blockquote">
            Vad gjorde han som var ädelt eller gudalikt? Gav han igen med samma mynt
            när han själv blev föraktad och förlöjligad? Om han var en gud – varför
            visade han då inte sin gudomliga makt genom att straffa eller hämnas på
            dem som kränkte honom eller hans Fader?
        </blockquote>

        <p class="chapter-text">
            Kyrkofadern Origenes, som svarade på dessa anklagelser, hävdade att Jesu
            verkliga gudomliga kraft uppenbarades just i hans uthålliga lidande för
            andras skull – och i hans vägran att håna tillbaka. För den romerska
            känslan var detta befängt. Så beter sig inte en gud.
        </p>

        <p class="chapter-text">
            I den antika världen fann man ära i förmågan att krossa och förödmjuka
            dem som satte sig emot en. En gud som inte slog tillbaka – som inte hånade
            sina hånare – var ingen gud alls.
        </p>

        <p class="chapter-text">
            Kristi korsfästelse, hans uppståndelse och hans gudomlighet – detta är
            ingredienserna i en tro som orsakade en "Big Bang". Den sände ut
            kulturella chockvågor som förvandlade rå maktutövning till synd och
            ödmjukhet till styrka.
        </p>

        <p class="chapter-text">
            Draget är så bisarrt att man kan undra om människor överhuvudtaget hade
            kunnat hitta på det. Bibeln antyder något annat: den framställer det som
            en "gudomlig dårskap" som överträffar det mänskliga förnuftet och
            undergräver grunden för den antika världsbilden.
        </p>

        <p class="chapter-text">
            Holland kom inte fram till denna insikt som en försvarare av tron. Han
            började som sekulär historiker och upptäckte, nästan mot sin vilja, hur
            djupt kristendomen har präglat den västerländska moralen. Många av de
            övertygelser som idag tas för givna – att alla människor är lika mycket
            värda, att man ska ta hand om de svaga, att man ska vara misstänksam mot
            okontrollerad makt – är varken tidlösa eller självklara. De är produkter
            av historien.
        </p>

        <p class="chapter-text">
            Om en modern humanist hade försökt förklara för en romare att hans slavar
            ägde inneboende värdighet och lika moraliskt värde, hade svaret sannolikt
            varit: Nej, det har de inte.
        </p>

        <p class="chapter-text">
            Detta väcker en besvärlig fråga för sekulär humanism. Några av dess
            djupaste moraliska reflexer har formats av kristendomen. Vad håller dessa
            reflexer vid liv när rötterna har skurits av? Kommer de att blekna eller
            så småningom glömmas bort? Kan de bestå under trycket från dagens växande
            tribalism?
        </p>

        <p class="chapter-text">
            Sekulära humanister menar att de kan det. Men Friedrich Nietzsche –
            kristendomens mest formidabla kritiker – hävdade, och till och med
            hoppades, att de har fel. Kort sagt menade Nietzsche att när Gud
            försvann skulle moralen förlora sin bindande kraft. Medkänsla, jämlikhet
            och omsorg om de svaga skulle gradvis förlora sitt grepp. Människor
            skulle inse att de inte längre hade något verkligt, tvingande skäl att
            göra gott, ingen djup motivering för att betrakta alla liv som lika
            mycket värda. Nietzsche förutsåg inte någon lugn eller rationell
            humanism. Han förutsåg en kris: antingen ett förfall i nihilism, där
            ingenting egentligen betyder något, eller framväxten av övermänniskan,
            som skulle återupprätta äldre, hedniska ideal om styrka och dominans.
        </p>

        <p class="chapter-text">
            Holland drar en liknande slutsats – men betraktar dess konsekvenser med
            oro snarare än hopp. Han liknar övergången vid en flerstegsraket.
            Kristendomen tillhandahöll de massiva "första stegen" – det teologiska
            bränslet från en gudomlig människa på ett kors – som gav den
            västerländska världen tillräcklig kraft för att undkomma den antika
            grymhetens gravitation. Idag har den sekulära humanismen kastat bort
            dessa steg som tomma skal. Den ser sig själv som en rymdsond som glider
            genom universum enbart med hjälp av rent förnuft.
        </p>

        <p class="chapter-text">
            Hollands farhåga är att utan det ursprungliga drivmedlet kommer sonden
            så småningom att tappa kursen – eller krascha spektakulärt.
        </p>

        <h2 class="chapter-heading">Livet utan en etisk polstjärna</h2>

        <p class="chapter-text">
            Låt oss återvända till huvudfrågan. Kan en moralkompass förbli
            tillförlitlig om den saknar ett positivt, Kristus-likt centrum? Vilka är
            konsekvenserna om man förlorar den "drivkraft" som levande traditioner en
            gång tillhandahöll? Vad händer när det mystiska och det övernaturliga
            skalas bort?
        </p>

        <p class="chapter-text">
            En risk blir omedelbart uppenbar: en rent negativ kompass kan skapa
            negativa drivfjädrar som vrede och rädsla. Ur rädslan för att upprepa en
            katastrof – parollen "aldrig mer" – skapas en stämning av oro, upprördhet
            och vaksamhet. Den offentliga debatten eskalerar snabbt till moralisk
            avsky. Förakt, snarare än kärlek, blir den främsta drivkraften. Vi
            riskerar att spegla just det hat som vi försöker motverka.
        </p>

        <x-quotecard>Om vi bekämpar ondskan med hat...</x-quotecard>

        <p class="chapter-text">
            En annan risk är lika uppenbar: människan längtar efter att tillhöra
            något som är större än henne själv. Den verkliga frågan är inte "tro eller
            ingen tro", utan snarare "tro på vad?". Att förkasta tron på Gud lämnar
            ett tomrum – och tomrum drar till sig substitut för religion. Alltför
            ofta fylls tomrummet av extremism och stamtänkande.
        </p>

        <p class="chapter-text">
            Det finns ett annat problem: Sekulär humanism kan erbjuda sunda etiska
            argument, men enbart argument är sällan en källa till motivation. De är
            ett svagt försvar mot maktens och korruptionens frestelser. Varför följa
            lagen eller reglerna om det är mer lönsamt att bryta mot dem och man kan
            komma undan med det? Den sekulära humanismen erbjuder kanske inte ett
            tillräckligt starkt motiv att inte fuska när ingen ser på.
        </p>

        <p class="chapter-text">
            När desillusionen slår rot, växer den sig starkare. Frågan är inte längre
            "vad är sant?" utan "vem tjänar på det?" Allt reduceras till ett maktspel
            där alla antas driva sina egna intressen. Man utgår från att alla är
            korrupta och att alla ljuger. Värderingar avfärdas som en fasad – något
            man upprätthåller för syns skull snarare än av övertygelse. Till slut
            spelar ingenting egentligen någon roll.
        </p>

        <p class="chapter-text">
            Denna glidning mot cynism kan lätt inträffa när en sekulär världsåskådning
            töms på bränsle. Under en tid kan tron på mänsklighetens framsteg hålla
            hoppet och energin vid liv. Men en sådan tro är skör och kan lätt knäckas
            av historiens tyngd.
        </p>

        <p class="chapter-text">
            Om det inte finns någon gudomlig måttstock eller platonska ideal, blir
            mänskligheten sin egen högsta auktoritet. "Människan är alltings mått."
            Men vilken människa? I praktiken tolkas detta ofta som: "Medelsvensson är
            alltings mått". Om du är bättre än de flesta är du en god människa.
            Resultatet: en självgod jämförelsekultur. Det gamla budet – "känn dig
            själv" – ersätts av "ranka dig själv".
        </p>

        <p class="chapter-text">
            Alla har blinda fläckar. Vi döljer alla saker för oss själva –
            misslyckanden, överträdelser och impulser vi helst inte vill benämna. När
            vi inte är medvetna om att sådana blinda fläckar finns, blir det lätt att
            anta att vårt eget perspektiv är klart, våra omdömen välgrundade och vår
            kultur står på mänsklighetens höjdpunkt. Man ser inte skillnaden mellan
            moralisk vrede och stammens vrede. "Vi är goda människor, så vi bör
            härska över världen." Tron på den egna civilisationens överlägsenhet
            öppnar dörren för allvarliga brott.
        </p>

        <p class="chapter-text">
            I den sekulära humanismen finns inget som överstiger människan. Dess
            populära credo – var sann mot dig själv – bildar en inkrökt fälla. Egot
            är en dålig domare över sig självt. Blinda fläckar är inte bieffekter
            utan en del av designen.
        </p>

        <p class="chapter-text">
            Det är frestande att skylla världens konflikter på religionen. Men den
            sekulära humanismen – modernitetens dominerande livsåskådning – kan visa
            sig vara mer en del av problemet än lösningen.
        </p>

