        <h2 class="chapter-heading">6. Måttfullhet: Lockas inte av ytterligheter</h2>

        <p class="chapter-text">
            Demagoger har en slug intelligens som gör att de kan samla på sig makt och
            rikedomar. De drivs av en omättlig girighet och glupskhet. För dem är
            "tillräckligt" och "förnöjsamhet" okända begrepp.
        </p>

        <p class="chapter-text">
            Det är ingen bra idé att välja ledare som inte kan styra sig själva! Deras
            brist på självkontroll – deras oförmåga att moderera sina begär – gör dem
            djupt olämpliga att leda. De har inte det som krävs för att förbättra
            världen: praktisk klokhet och gott omdöme. Eftersom de underblåser
            splittring och polarisering har de en tendens att inta radikala positioner
            och ha obalanserade åsikter.
        </p>

        <p class="chapter-text">
            Enligt Aristoteles är praktisk klokhet relaterad till måttfullhet och
            självkontroll. Ofta handlar det om att hitta den gyllene medelvägen och
            inte gå för långt åt något håll. I beslutssituationer är det viktigt att
            inte överreagera eller underreagera.
        </p>

        <p class="chapter-text">
            Ibland måste vi ta tydlig ställning i en fråga. Men mycket ofta måste vi
            hitta den rätta balansen mellan två motsatser. Det kan till exempel handla
            om
        </p>

        <ul>
            <li>idealism och realism</li>
            <li>enhet och mångfald</li>
            <li>kollektivism och individualism</li>
            <li>ordning och frihet.</li>
        </ul>

        <p class="chapter-text">
            Det gäller att kunna hålla ihop en komplicerad verklighet – att kunna
            integrera.
        </p>

