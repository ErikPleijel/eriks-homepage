            width="15" />

        <p class="chapter-text">
            I sin iver att känna sig mäktiga har dessa anhängare kompromissat med sin
            integritet. De märker inte att de blivit fångar i sina egna destruktiva
            impulser. Dra i trådarna av rädsla, stolthet eller vrede – och de reagerar
            som marionetter. I sin förtrollning kan de inte längre höra förnuftets
            eller medkänslans röster. Även de har accepterat avtalet. Men vad finns det
            att vinna om priset är din själ?
        </p>

        <p class="chapter-text">
            Det finns ett sätt att säga 'nej tack' till pakten – och behålla sin
            frihet och integritet. Den som inte vill bli utnyttjad som en marionett
            behöver bygga upp en inre styrka och karaktär.
        </p>

        <p class="chapter-text">
            I århundraden har människor förlitat sig på ett uråldrigt skydd mot denna
            mörka frestelse: de sju klassiska dygderna. Att stå fast i detta ständigt
            återkommande drama förutsätter att man kultiverar tro, hopp, kärlek,
            vishet, rättrådighet, måttfullhet och mod. Detta är det klassiska sättet
            att bygga motståndskraft mot den faustiska pakten.
        </p>

