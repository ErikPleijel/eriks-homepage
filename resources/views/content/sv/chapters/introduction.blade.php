        <h2 class="chapter-heading">Självbemästring – den glömda nyckeln till demokratins överlevnad</h2>

        <p class="chapter-text">
            Ordet 'dygd' har länge avfärdats som en kvarleva från det förflutna. I dag
            lär vi oss detta den hårda vägen: utan dygd kollapsar demokratin.
        </p>

        <p class="chapter-text">
            Dygd är inte en uppvisning i moralisk överlägsenhet eller att skylta med
            sin godhet. Det är snarare konsten att skapa harmoni mellan förnuft, begär
            och känslor.
        </p>

