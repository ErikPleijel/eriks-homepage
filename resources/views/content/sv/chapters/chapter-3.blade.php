        <h2 class="chapter-heading">Ett befriande vågspel</h2>

        <p class="chapter-text">
            Enligt den lutherska tron är alla religiösa mutförsök verkningslösa.
            Tron att man kan ha kontroll över sitt eget öde är en illusion. Detta
            är i själva verket ett glatt budskap. Vi behöver inte tro på grund av
            rädsla, fåfänga och kontrollbehov. I stället öppnas vägen för en
            frivillig tro som berör på rätt punkt.
        </p>

        <p class="chapter-text">
            Bonhoeffer skrev:
        </p>

        <blockquote class="chapter-blockquote">
            När man fullständigt uppgivit att göra sig till något särskilt – det
            må vara helgon eller en omvänd syndare eller en kyrkoman, en rättfärdig
            eller orättfärdig, sjuk eller frisk […] då kastar man sig helt i Guds
            armar […] och detta, menar jag, är att tro, är omvändelse, och så
            blir man en människa, en kristen.<x-footnote-trigger
                label="2"
                text="Hela citatet: «Senare har jag fått erfara, och erfar det den dag idag, att man först lär sig tro genom helt och fullt jordeliv. När man fullständigt uppgivit att göra sig till något särskilt – det må vara helgon eller en omvänd syndare eller en kyrkoman (en så kallad mönsterpräst), en rättfärdig eller orättfärdig, sjuk eller frisk – och detta kallar jag dennasidighet, att leva mitt uppe i rikedomen av uppgifter, problem, framgångar och misslyckanden, erfarenheter och rådvillhet – då kastar man sig helt i Guds armar, då tar man inte längre sina egna lidanden, utan Guds lidanden i världen på allvar, då vakar man med Kristus i Getsemane; och detta, menar jag, är att tro, är 'metanoia' (omvändelse), och så blir man en människa, en kristen.» (Ur Motstånd och underkastelse.)" />
        </blockquote>

        <p class="chapter-text">
            Denna övning i tillit gör en del oroliga och villrådiga. De tänker:
            "Det måste väl ändå finnas någon egenskap man kan lyfta fram för att
            visa att man inte är på samma nivå som de feltroende? Annars är man
            ju bara en vanlig människa! Och är det inte riskabelt att vara helt
            beroende av Guds vilja? Då har man ju ingen som helst kontroll över
            situationen!"
        </p>

        <p class="chapter-text">
            Varför detta behov att jämföra sin tro med andras? Varför denna önskan
            att göra nåden till en rättighet? Vill man inte släppa taget kan
            äventyret aldrig börja.
        </p>

        <p class="chapter-text">
            Tron är ingen magisk formel som gör Gud vänligt sinnad. Tron handlar
            inte heller om att prestera ett visst själstillstånd för att få en
            biljett till himlen. Att tro är att kasta sig i Guds armar och lita
            på att han är god och vill hjälpa oss. Först då är det möjligt att
            skifta fokus till det som har verklig betydelse. Och först då är det
            möjligt att ta Gud och den kristna tron på verkligt allvar.
        </p>

        <h2 class="chapter-heading">Ingen dressyr för människor</h2>

        <p class="chapter-text">
            För en del är det mer logiskt att en rätt tro ska löna sig. De tror
            på Jesus för att få vissa exklusiva förmåner. "Vad tjänar det annars
            till att vara kristen?", undrar de.
        </p>

        <p class="chapter-text">
            Är det inte bättre att följa honom för att man verkligen vill göra
            det? En sann tro är frivillig och behöver ingen belöning. Belöning
            och straff kan användas för att dressera djur. En människa bör
            utvecklas på ett annat vis: som en fri och ansvarig person med inre
            integritet.
        </p>

        <p class="chapter-text">
            Det viktigaste i livet är sådant som medkänsla, omdöme, självinsikt,
            ödmjukhet, sanningskärlek, ansvarighet och integritet. Sådana här
            saker kan inte köpas eller tvingas fram. De sanna värdena kan bara
            kultiveras i en fri relation. Detta kan bli verklighet när vi tror att
            Guds kärlek är en omsorg om själens hälsa.
        </p>

        <p class="chapter-text">
            Gud och människa förenas i ett gemensamt projekt – det är detta som
            är innebörden i uttrycket "att följa Kristus".
        </p>

        <h2 class="chapter-heading">Följ denne unge man</h2>

        <p class="chapter-text">
            För att få kalla sig kristen krävs två saker: att man är döpt, och att
            man vill följa en ung man som gick på jorden för tvåtusen år sedan. En
            del av hans ord och handlingar kan vara svåra att förstå eftersom han
            levde i en annan tid och kultur. Men det är tydligt att han hade mod
            och en mycket stark känsla av syfte och mening.
        </p>

        <p class="chapter-text">
            Det sägs att han levde i öknen i fyrtio dagar för att visa sin förmåga
            att stå emot frestelser. Djävulen erbjöd honom alla världens riken om
            han ville dyrka honom. Han avvisade erbjudandet och valde i stället den
            frivilliga ödmjukhetens och självuppoffringens väg. Från den stunden
            predikade han, utförde mirakel och botade sjuka.
        </p>

        <p class="chapter-text">
            Till moralisternas förtrytelse sökte han upp allt möjligt dåligt folk
            och åt och drack med dem. Han försökte förklara för människorna att
            Gud älskar syndare lika mycket som de rättfärdiga. Därför måste vi
            göra samma sak och visa respekt för dem som inte förtjänar det, i
            synnerhet våra ovänner. Vi får inte behandla våra fiender på samma
            sätt som de behandlar oss.
        </p>

        <p class="chapter-text">
            Han varnade för de religiösa pedanterna som är fixerade vid
            oväsentligheter och lägger tunga bördor på människorna. Varför bedömer
            ni inte själva vad som är rätt? frågade han. Alla bud bygger på
            kärleken till Gud och den gyllene regeln. En gång gjorde han sina
            lärjungar helt förbluffade när han tvättade deras fötter och visade
            att den som tjänar är större än den som härskar.
        </p>

        <p class="chapter-text">
            På den yttersta dagen kommer alla människor att få sina liv utvärderade,
            sade han. De som varit hårda och dömande mot andra kommer då att mätas
            med samma måttstock och förstå att de förtjänar ett hårt och oförsonligt
            straff. På den dagen ska det bli klart att det viktigaste av allt är
            att bry sig om dem som är sämre lottade, i synnerhet de fattiga. Något
            som i synnerhet upprör Gud är när man kallar en annan människa för ett
            dumhuvud, menade han.
        </p>

        <p class="chapter-text">
            När han kallade sina lärjungar erbjöd han dem inte ett liv i
            bekvämlighet och välstånd. Tvärtom sade han att de inte kan förvänta
            sig några belöningar eller förmåner. De som följer honom kan inte heller
            räkna med att ha total kontroll över sina liv eller veta vart de är på
            väg. Han menade att de är deltagare i ett stort historiskt drama som
            ingen, inte ens han själv, har någon fullständig överblick över.
        </p>

        <p class="chapter-text">
            Tillit till den himmelske fadern var vad han ville lära dem. Endast
            genom mig, sa han, kommer ni att finna sådan tro och frihet. Det är
            detta som är vägen, sanningen och livet.
        </p>

        <p class="chapter-text">
            Det var sådana här saker som denne unge man gick omkring och
            undervisade om. Att följa i hans fotspår innebär först och främst att
            lära sig att inte hata och förakta.
        </p>

