        <p class="chapter-kicker">Kapitel 3</p>
        <h1 class="chapter-title">Tillit: Trons vågspel startar ett äventyr och ger livet en riktning</h1>

        <p class="chapter-lead">
            En tro som kräver belöningar föder rädsla och fåfänga. Men tillit –
            verklig tillit – öppnar dörren till integritet, förnuft och medkänsla.
        </p>

        <h2 class="chapter-heading">Ett andligt vaccin</h2>

        <p class="chapter-text">
            Kyrkans symboler och mysterier kan verka egendomliga och uråldriga.
            Många avvisar dem föraktfullt som "gamla dogmer". Vi behöver förstå hur
            kraftfulla symboler kan vara. De berör på en djupare nivå och kan vara
            mycket starkare än ord och begrepp. Bilden av korset har påverkat hela
            vår kultur. Det var en vändpunkt i historien.<x-footnote-trigger
                label="1"
                text="Läs t.ex. Dominion: How the Christian Revolution Remade the World av Tom Holland." />
        </p>

        <p class="chapter-text">
            Före Kristus var korset en ren maktsymbol. Det var inget annat än ett
            brutalt avrättningsredskap. Den representerade romarrikets förmåga att
            förslava människor genom att skrämma dem till underkastelse.
        </p>

        <p class="chapter-text">
            Efter Kristus blev korset det rakt motsatta: en symbol för kärlek.
            Detta är ett radikalt bildspråk. Det är en berättelse om Guds
            medlidande med de maktlösa. Gud befinner sig inte någonstans långt
            borta och beskådar världens elände. Genom Kristus kommer han ned till
            vår jordiska och mänskliga nivå. Korset betyder att han lider med sin
            skapelse. Uppståndelsen betyder att han älskar den ändå och att han
            vill förlåta. Han utplånar inte sina fiender i eld och svavel, utan
            övervinner viljan att hämnas.
        </p>

        <p class="chapter-text">
            Många tror på Gud eftersom han har makt och kan bringa lycka och
            framgång. De säger att de älskar honom, men i själva verket är det
            gåvorna och makten de älskar. Berättelsen om korset skapar en
            förändring i var och en som tror på den och fattat den. Makt och
            maktutövning upphör att fascinera. Gudsdyrkan är inte lika med
            makt-dyrkan.
        </p>

        <h2 class="chapter-heading">Valet: Kontroll eller tillit</h2>

        <p class="chapter-text">
            I ljuset av detta blir det uppenbart hur naivt det är att använda tron
            som en hållhake för att få kontroll över Gud. Det är en himmelsvid
            skillnad mellan dessa två påståenden:
        </p>

        <ul>
            <li>"Min tro på Kristus ger mig rätt att få en plats i paradiset."</li>
            <li>"Budskapet om Kristus ger mod att lita på att Gud är god."</li>
        </ul>

        <p class="chapter-text">
            Ligger frälsningen innanför eller utanför människans kontroll? Om vi
            vill uppleva att vi har saken inom vår kontroll, måste vi vara bergfast
            övertygade om att vi har rätt teologi och rätt fromhet. Det här skapar
            en enorm rädsla för att tänka fel. Resultatet blir en stark tro på sin
            egen felfrihet. Men om alltihop ligger utanför vår kontroll, finns
            inget annat att göra än att förtrösta på Gud själv.
        </p>

