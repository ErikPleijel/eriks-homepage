        <h2 class="chapter-heading">I tjänst för något högre</h2>

        <p class="chapter-text">
            Få har förkroppsligat detta kall så fullständigt som Dag Hammarskjöld.
            Den svenske diplomaten tjänstgjorde som FN:s generalsekreterare från
            1953 till sin död i en flygkrasch över Kongo 1961 – i tjänst till det
            allra sista.
        </p>

        <p class="chapter-text">
            Han förstod att när han väl hade avlagt ämbetseden, var han inte längre
            lojal mot en nation, utan mot Sanningen. Han skulle inte tjäna Sverige,
            eller Väst, eller någon "koalition av de rättfärdiga", utan FN-stadgan.
            När han väl hade tagit på sig domarens mantel, fick han inte ta parti.
        </p>

        <p class="chapter-text">
            Kraven på den rollen var höga: opartiskhet gentemot särintressen, mod
            att stå emot påtryckningar från stormakterna, respekt för lag och
            sanning, rättrådighet som vägleds av principer snarare än makt,
            integritet även när det kostar – osjälvisk tjänst som utplånar egot.
            Detta är klassiska dygder i institutionella kläder.
        </p>

        <p class="chapter-text">
            Han var ingen naiv idealist. Han var en hård realist som insåg den
            obehagliga sanningen: dessa institutioner är det enda skyddet mot
            avgrunden där makt går före rätt. "FN skapades inte för att föra
            mänskligheten till himlen, utan för att rädda den från helvetet", sade
            han.
        </p>

        <p class="chapter-text">
            Det är detta som det innebär att vara civiliserad: att ta avstånd från
            dubbelmoral och tillämpa samma normer för alla. Den verkliga konflikten
            står inte mellan civilisationer, utan mellan ett civiliserat spel och
            ett ociviliserat kaos.
        </p>

        <p class="chapter-text">
            Men han var ingen själlös byråkrat. Han kunde bära sin tunga roll genom
            ett djupt kontemplativt inre liv – som man kan läsa om i hans Vägmärken.
            Dessa texter vittnar om att han i sitt privatliv var hängiven Gud. Hans
            inåtvända, mystiska kristendom gav honom inte en identitet och
            grupptillhörighet. I stället gav den honom styrkan att ägna sitt liv åt
            något som var större än han själv.
        </p>

