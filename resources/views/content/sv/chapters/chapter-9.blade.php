        <p class="chapter-text">
            Populister är som fotbollsfans som har tagit sitt hat mot domaren till
            en helt ny nivå. Alla dessa ansiktslösa, byråkratiska och tidvis korrupta
            institutioner borde rivas ner. Det går aldrig upp för dem att de
            förstör det spel de älskar.
        </p>

        <p class="chapter-text">
            Dessa institutioner är inte något vi hyser varma känslor för – de är
            inte något vi "gillar". Vad som behövs är en annan sorts kärlek: omsorg,
            ansvar och vaksamhet. De måste bemannas av rättrådiga och kompetenta
            människor. Varje fungerande samhälle är beroende av deras tysta arbete,
            även när det är svårt att förstå och långt ifrån våra dagliga liv.
        </p>

        <p class="chapter-text">
            Allt detta är en utvidgning av åtagandet till den sekulära staten och
            den gyllene regeln. Opartiskhet och ömsesidighet innebär att samma
            normer gäller för alla – de mäktiga såväl som de svaga. En regelbaserad
            ordning är en tillitsbaserad ordning. Den skapar osynliga band som
            håller världen samman.
        </p>

