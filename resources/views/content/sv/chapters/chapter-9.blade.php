        <p class="chapter-lead">
            Många lockas av visionen om ett rent religiöst samhälle. Men det är en
            idé som riskerar att korrumpera tron genom makt och privilegier.
        </p>

        <h2 class="chapter-heading">En farlig dröm</h2>

        <p class="chapter-text">
            "Kristen nationalism" rymmer en inre motsättning: det kristna handlar
            om att lära sig ödmjukhet, medan nationalismen kretsar kring stolthet,
            som ibland slår över i högmod. Det behöver inte vara en omöjlig
            kombination – men då krävs det att vi undviker en särskild fälla.
        </p>

        <p class="chapter-text">
            Kristen nationalism är drömmen om en enhetlig nation som hålls samman
            av en stark känsla av identitet och gemenskap. Men drömmen riskerar att
            förvandlas till en mardröm när statens maktmedel börjar användas för
            att driva igenom visionen. Då handlar det inte längre om gemenskap, utan
            om att stöta bort, forma om eller till och med utplåna det som inte
            passar in. Hänsynslösheten ligger inbyggd i projektet. I den meningen
            kan kristen nationalism bli radikalt okristen.
        </p>

        <p class="chapter-text">
            Många tänker sig att "det sekulära" är ett ateistiskt projekt, skapat
            för att tränga undan tron till det privata. Men inget kunde vara mer
            felaktigt. I själva verket är det sekulära – hur motsägelsefullt det än
            låter – en djupt kristen tanke.
        </p>

        <p class="chapter-text">
            Historikern Tom Holland beskriver i boken Dominion hur allt började med
            Jesu ord: "Ge kejsaren det som tillhör kejsaren och Gud det som tillhör
            Gud." Där låg fröet till en omvälvande idé.
        </p>

        <p class="chapter-text">
            I den antika världen var religion och politik oupplösligt sammansmälta.
            Där fanns ingen gräns, inget avstånd, ingen neutral zon. Härskaren
            styrde med gudomlig auktoritet, och staten vilade på religiös
            legitimitet. Först långsamt började de kristna ana att dessa sfärer inte
            borde blandas, utan skiljas åt. Denna insikt har slipats genom seklerna
            – i medeltidens kamp mellan påve och kejsare, i reformationens
            åtskillnad mellan andligt och världsligt och i upplysningens tankar om
            frihet.
        </p>

        <p class="chapter-text">
            Ingen vet exakt hur gränsen ska dras, men grundprincipen är tydlig: det
            måste finnas en neutral zon där tron inte blir ett maktmedel. Den
            sekulära staten skapar ett andrum som skyddar tron från att korrumperas
            av politiska privilegier.
        </p>

        <p class="chapter-text">
            Detta ska inte förväxlas med sekularisering, den process där religion
            gradvis förlorar inflytande över samhället och människors vardag. Den
            sekulära staten handlar i stället om hur vi hanterar den laddade
            relationen mellan tro och makt. Rätt förstådd kan den fungera som ett
            viktigt skydd mot religiös tvång – och lika viktigt, mot ateistiskt
            tvång.
        </p>

        <h2 class="chapter-heading">Skapa utrymme för den gyllene regeln</h2>

        <p class="chapter-text">
            Att samhället genomsyras av kristna värderingar är i sig inget problem
            – och på många sätt är det redan så. Jesus sammanfattar lagen i den
            gyllene regeln: "Allt vad ni vill att människorna ska göra för er, det
            ska ni också göra för dem." Denna princip om ömsesidighet bär upp
            mycket av vår moderna rättsordning.
        </p>

        <p class="chapter-text">
            Kanske det mest kristliga vi kan göra är att slå vakt om detta så att
            det inte raseras av olika religiösa överhöghetsprojekt. Vi får aldrig
            använda statens resurser och lagar för att skaffa oss privilegier
            gentemot till exempel judar, muslimer och icke-troende.
        </p>

        <p class="chapter-text">
            För när tron blir en väg till makt och status säljer vi vår själ i en
            faustisk pakt. Då förvandlas tron till något mutat, förvridet och till
            sist tomt.
        </p>

        <p class="chapter-text">
            Den sekulära staten gör inte tron urvattnad – tvärtom. Den gör det
            möjligt för kristna att faktiskt leva som kristna i samhället och
            praktisera den gyllene regeln.
        </p>

        <p class="chapter-text">
            Det handlar också om att öva sig i måttfullhet. Nationalstolthet
            fungerar ungefär som alkohol. I små doser kan den ge mod och stärka
            självkänslan. I större doser grumlas omdömet. Till slut blir vi hårda
            och aggressiva. När detta blandas med religiös självrättfärdighet skapas
            en giftig cocktail som förhärdar hjärtat och förstör omdömet.
        </p>

        <x-quotecard>Nationell stolthet fungerar som alkohol...</x-quotecard>

        <p class="chapter-text">
            Naturligtvis finns det en sund nationalstolthet som bygger på
            måttfullhet. En sådan stolthet och identitet bör vila på tanken om en
            sekulär och neutral stat. Detta är ett urgammalt arv, i mycket präglat
            av kristen tradition, som vi bör bevara och utveckla. Det skyddar inte
            bara främlingar och minoriteter. Det skyddar också oss kristna – inte
            minst från oss själva.
        </p>

        <h2 class="chapter-heading">Vem hejar på domaren?</h2>

        <p class="chapter-text">
            Ett av de mer sunda uttrycken för nationalism och stamtänkande är
            fotboll. I denna sport håller folk på sitt eget lag. Det hörs
            passionerade sånger, ramsor och hejarop. Men vem hejar på domaren?
            Ingen. Han rör sig genom matchen som en anonym gestalt i en otacksam
            roll – visslar, pekar, och förstör ibland vår glädje. Han hetsas och
            hånas. Gör han ett bra jobb glömmer man snart att han ens fanns. Han
            väcker ingen hängivenhet.
        </p>

        <p class="chapter-text">
            Och ändå – utan honom finns det ingen match. Ingen tänker: "Han gör ett
            dåligt jobb, därför behövs ingen domare." Tar man bort domaren blir
            spelet mer våldsamt. Snart har man något som inte ens liknar fotboll.
        </p>

        <p class="chapter-text">
            Det är laget som vi älskar. Men bakom all passion finns insikten att
            spelet är helt beroende av någon som ingen älskar: domaren. Detta
            förstår vi instinktivt, även om vi aldrig har satt ord på det.
        </p>

        <p class="chapter-text">
            Vi förstår också att det måste finnas någon som utser domaren. Det
            måste finnas ett nationellt fotbollsförbund som fastställer reglerna,
            utser funktionärer och hanterar överklaganden. Och ovanför detta finns
            ett internationellt organ – som FIFA. Dessa organisationer är
            ansiktslösa, byråkratiska, ibland korrupta – och helt nödvändiga. Trots
            alla skandaler kring FIFA är det ingen som på allvar föreslår att
            organisationen inte borde finnas. Frågan är aldrig om vi behöver
            styrande organ, utan om de sköter sitt uppdrag hederligt.
        </p>

        <p class="chapter-text">
            Fotbollsvärlden är, i miniatyr, en modell för något mycket större. Varje
            fungerande samhälle har domare – tillsynsmyndigheter, domstolar,
            internationella organ. De är oansenliga av en anledning. Deras roll är
            inte att spela spelet, utan att göra spelet möjligt. Ju bättre de
            sköter sitt uppdrag, desto mindre märker vi av dem.
        </p>

        <p class="chapter-text">
            Regler, lagar och procedurer väcker sällan någon entusiasm. De avbryter
            oss, begränsar oss och bromsar oss. De säger "nej" när vi vill ha ett
            "ja" och "vänta" när vi vill ha det "nu". Precis som domaren som står
            mitt i matchen, är de impopulära och ofta föremål för missnöje. De
            märks mest när de gör oss besvikna.
        </p>

        <p class="chapter-text">
            Avlägsna och anonyma institutioner är sällan något man känner
            tillgivenhet för. Vem sjunger hymner om Världshandelsorganisationen?
            Vem skriver dikter om Internationella atomenergiorganet?
        </p>

        <p class="chapter-text">
            Och ändå – utan opartiska institutioner finns det inget liv som vi
            känner det. Det är de som gör friheten möjlig. Förmågan att lita på en
            fullständig främling – att skriva på ett kontrakt, gå ombord på ett
            flygplan, dricka vatten, ta emot en sedel – är ett stilla mirakel. Vi
            tänker inte mycket på det. Men vi borde göra det.
        </p>

        <p class="chapter-text">
            Om dessa institutioner avskaffas är det inte frihet som återstår, utan
            kaos och våld. Stater och institutioner blir alltmer maffialiknande.
        </p>

